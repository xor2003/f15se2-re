<h4 class="h4">Copy the Maps</h4>
<p class="p1">One thing that helps keep <i>F-15 Strike Eagle</i> interesting is the variety of ways to complete each mission. Plan your mission on a photocopy of the mission map and keep track of successful routes. This is what real fighter pilots do, and it's a good idea for you as well.</p>
<p class="p">Navigation, fuel planning, and examination of enemy air defenses is just as much a part of an actual military operation as the time spent flying. It makes good sense for you to take these items into account before flying a mission.</p>

