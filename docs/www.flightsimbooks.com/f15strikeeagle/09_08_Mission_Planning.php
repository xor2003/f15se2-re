<h3 class="h3"><b>Mission Planning</b></h3>
<p class="p1">Knowing what you're going to do and how you're going to do it before the wheels ever leave the tarmac is vital to mission success. Although very few plans stand up to the stress of combat, it's much better to have a plan and change it than to have no plan at all.</p>
<h4 class="h4"><b>Mission Selection</b></h4>
<p class="p1">Even at the same difficulty level, some missions are much easier than others. The type of enemy SAM sites and the location of the targets can be just as important as the level of difficulty you've chosen. If it's your first time at a high-difficulty level, or you're going for one of the top medals, be selective about the mission you choose.</p>
<p class="p">Missions where the primary and secondary targets are very far apart and/or deep behind the lines are very tough. For an easier time at Ace level, keep restarting the game until you get a mission where both targets are close to each other and are fairly close to your base.</p>
<h4 class="h4"><b>Ingress</b></h4>
<p class="p1">Getting to the target can be the easiest part of the mission, or the toughest, depending upon a number of concerns. Unfortunately, you have little control over most of these concerns, but there are a few things you can do to improve your odds.</p>
<p class="p">First, the enemy is expecting you to come from the direction of your base and will keep a good lookout. If you take off and approach the target from a different angle, chances are you won't be spotted until you're much closer to the target area.</p>
<a id="page_168"></a>
<p class="p">Second, go in low. The lower you fly, the harder it will be to spot you on radar&#8212;the lower the better.</p>
<p class="p">Take out any troublesome SAM sites on the way in. That way, you won't have to deal with them on the way out. This is especially important if you have to fly between two SAM sites to get to the target area.</p>
<p class="p">You'll be in good shape while you're flying between them, since you'll be at right angles to both of them and neither will be able to get a lock on you. As you go beyond them, however, you'll run into trouble. As you're about to attack the target, both sites will be launching missiles at your rear quarter. It may be impossible to get perpendicular to both of them, and if they're both Doppler systems, you'll be in trouble. For this reason, it's best to take out one of the sites on your way in. This may mean facing a couple of missiles while you're attacking the site, but they're much easier to deal with head-on.</p>
<h4 class="h4"><b>Egress</b></h4>
<p class="p1">After you've taken care of the primary and secondary targets, it's time to look for a way home. A straight line may not be the best course. If possible, take a course that puts most or all of the remaining SAM sites off to one side. It will be easier to avoid the SAMs if they're all coming from the same direction.</p>
<p class="p">This is also the time to pick out likely targets for any remaining Mavericks you may have on board. Those missile boats you avoided on the way in can make great targets on your way home. If things are quiet, you may want to pick a couple of targets for strafing. Extra ground targets really add to your point total.</p>
<p class="p">Another important thing to consider when heading home is which airbase to head for. Your navigation system will position the cursor on the HUD to guide you to the nearest base once both missions are complete. However, this may not be the best place to land. You'll generally attract a lot of attention while completing your missions, so it may be best to land a little farther away from the bad guys. Trying to line up a landing is hard enough without having to dodge SAMs and enemy aircraft at the same time.</p>

