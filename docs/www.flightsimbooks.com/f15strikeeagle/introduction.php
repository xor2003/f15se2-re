<p class="p"><b>Purpose and Scope.</b> This handbook provides guidance for commissioned and flight-rated officers assigned to the Eagle Rapid Deployment Team (ERDT). A certain level of flight proficiency and professionalism is expected for those receiving this assignment, so some items may be covered in more detail than others. Historical background is discussed, as is the development of your aircraft, the F-15E Strike Eagle. Policy, practices, directives, and procedures common to the efficient operation of this aircraft in the accomplishment of your assigned mission are covered in some detail.</p>
<p class="p"><b>Explanation of Terms.</b> Abbreviations and acronyms are used extensively throughout this handbook. They'll be defined or explained upon first use. See the end of this publication for a complete list of all abbreviations and acronyms.</p>
<p class="p">Throughout this publication, <i>aerospace</i> and <i>air</i> are used interchangeably. The use of the term <i>air</i> should not be construed as the more limited definition of the aerospace medium.</p>
