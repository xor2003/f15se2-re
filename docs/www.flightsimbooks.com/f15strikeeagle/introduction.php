<tr>
<td align="justify"><p>To deploy rapidly, worldwide, in response to threats to national security interests. To successfully fly air interdiction missions, day or night, in all weather, alone or with other aircraft, against tactical and point targets, deep in enemycontrolled territory without the need for dedicated fighter escort.</p></td>
