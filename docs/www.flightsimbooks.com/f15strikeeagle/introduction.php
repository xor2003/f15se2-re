<p class="p"><b>Military Doctrine.</b> <i>Doctrine</i> is defined as:</p>
<p class="p6">Fundamental principals by which the military forces or elements thereof guide their actions in support of national objectives. It is authoritative but requires judgment in application.</p>
<p class="p">Aerospace doctrine is a statement of officially sanctioned beliefs and war-fighting principals that describe and guide the proper use of aerospace forces in military action. The Air Force promulgates and teaches this doctrine as a common frame of reference on the best way to prepare and employ aerospace forces.</p>
<p class="p">Aerospace doctrine is an accumulation of knowledge that's gained primarily from the study and analysis of experience. As such, doctrine reflects what has usually worked best. These experiences may include actual combat operations as well as tests, exercises, or maneuvers. In those less frequent instances in which experience is lacking or is difficult to acquire (theater nuclear operations), doctrine may be developed through analysis of postulated actions.</p>
<p class="p">Aerospace doctrine has grown from the need to establish common guidelines for military action. These guidelines are particularly important under the stress of combat. For ex<a id="page_xi"></a>ample, if a subordinate is unable to communicate with his commander and follows the established doctrine, his actions will normally follow his commander's recommended course of action and support the larger scheme of operations. This example describes the prescriptive nature of doctrine, but it should be emphasized that doctrine provides a recommended course of action. It isn't mandatory. As stated, it requires &ldquo;judgment in application.&rdquo;</p>
<p class="p">The Air Force has articulated aerospace doctrine at three different levels and depths of detail in the forms of <i>Basic, Operational</i>, and <i>Tactical</i> doctrine.</p>
<ul>
<li><p class="p1">Basic Doctrine states the most fundamental and enduring beliefs that describe and guide the proper use of aerospace forces in military action. It is the foundation of all aerospace doctrine.</p></li>
<li><p class="p1">Operational Doctrine applies the principles of basic doctrine to military actions by describing the proper use of aerospace forces in the context of distinct objectives. It anticipates changes that affect the aerospace doctrine, such as technological advances.</p></li>
<li><p class="p1">Tactical Doctrine applies basic and operational doctrine to military actions by describing the proper use of specific weapons systems to accomplish detailed objectives. Tactical doctrine considers the tactical objective (mine a harbor from the air) and tactical conditions (threats, weather, terrain), and describes how a specific weapons system is employed to accomplish the objective (B-1s lay the aerial mines from low altitude).</p></li>
</ul>
<p class="p">An example of this doctrine would be:</p>
<p class="p6"><b>Basic Doctrine.</b> An important goal in air warfare is to gain freedom of action in the air environment.</p>
<p class="p6"><b>Operational Doctrine.</b> An air commander employs forces to obtain air superiority by orchestrating offensive and defensive counter-air operations, suppressing enemy air defenses, and coordinating support actions.</p>
<a id="page_xii"></a>
<p class="p6"><b>Tactical Doctrine.</b> F-15s fly sorties such as <i>combat air patrol</i> (CAP) in certain numbers and in certain formations. Tactical doctrine describes how CAP missions may be integrated with other weapons systems.</p>
<p class="p">In your situation as a flying officer, you shouldn't have to concern yourself with Basic and Operational doctrine, as they'll be established and passed down from higher authority. Tactical doctrine, however is an immediate concern and will be the focus of this handbook.</p>

