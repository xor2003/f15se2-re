<h4 class="h4"><b>Landing (Carrier)</b></h4>
<p class="p1">Carrier landings in this simulation really aren't that much harder than runway landings. In fact, due to the nature of carrier landings, you can hit the flight deck much harder than when landing on a runway.</p>
<p class="p">Your line-up and approach should be the same as for a runway landing, but you might want to make a steeper approach so you can get a good look at the carrier deck and the arrestor cables. The landing portion of the carrier deck is canted 15 degrees, so to use that part of the deck for landing, you need to land on a heading of 165 degrees. It really doesn't matter, however; landing straight-in on a 180-degree heading will work just fine.</p>
