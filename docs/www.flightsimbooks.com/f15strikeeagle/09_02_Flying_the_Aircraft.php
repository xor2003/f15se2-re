<a id="page_155"></a>
<p class="p">Unlike runways, which work fine in either direction, you can only catch the cables on a carrier from the north (headed south on a 180-degree heading). There are no cables to catch you on the front of the ship. Landing on a 000 heading is possible, but there are no cables to stop you. However, if you come in very close to stall speed with the brake on, you should be able to stop on your own. You may then have to taxi around to get enough deck in front of you to take off again.</p>

