<h3 class="h3"><b>Flying the Aircraft</b></h3>
<h4 class="h4"><b>Takeoff</b></h4>
<p class="p1">If you're flying in Rookie mode, you won't have to worry about taking off&#8212;you'll already be flying when the screen comes up. Other modes require you to take off manually. The process is very simple: Just hit the Afterburner key and let the plane roll until you're above stall speed; then ease back on the stick. There are two things you should also keep in mind. First, if you're too light on the stick, you may only lift the nose up. It may look like you're flying, but the main gears are still firmly on the ground. When the altitude indicator in the right side of the HUD begins to move, you're airborne. Second, don't wait too long to lift off. If you run off the end of the runway at high speed, both you and your aircraft will be destroyed&#8212;hardly a good way to start and finish a mission.</p>
<h4 class="h4"><b>Landing (Runway)</b></h4>
<p class="p1">The most important&#8212;and difficult&#8212;part of learning to land is learning how to get properly lined up with the runway. Imagine that the runway isn't a short concrete strip but, instead, a straight highway that extends for miles to the north and south. (Remember that all runways and carriers in <i>F-19</i> are lined up pointing north-south.) This is a long and wide highway, but you can only land on the portion near the base.</p>
<p class="p">If this were the case, getting lined up would be easy: You could just fly over to the highway, turn, and follow it all the<a id="page_152"></a> way back to the base. You would be perfectly lined up. Learning how to use a couple of the instruments you have on board will allow you to do this.</p>
<h4 class="h4"><b>Getting Lined Up</b></h4>
<p class="p1">The instruments you really need to pay attention to in order to get properly lined up are the <i>heading indicator</i> along the top of the HUD and the <i>NAV cursor</i> (the small triangle-shaped pointer that moves along the heading indicator to guide you toward different locations).</p>
<p class="p">To see how to use these instruments to line up for a landing, you can set up the game up as follows and try the procedure.</p>
<p class="p1"><b>Mission:</b> Libya</p>
<p class="p1"><b>Targets:</b> Pick a mission with targets in the southeastern area&#8212;around Benghazi and Benina.</p>
<p class="p1"><b>Mode:</b> Pilot</p>
<p class="p1"><b>Training:</b> Once airborne, hit Alt-T to switch to Training mode (now enemy weapons won't damage your aircraft). Fly to the targets and take them out. You can do this on afterburners if you like&#8212;you can hit Alt-R to refill the gas tank and reload weapons.</p>
<p class="ul">&bull; Now that you're ready to head for home, you should be heading north. Make your heading 000. Once you go &ldquo;feet wet&rdquo; over the water, your heading indicator should look like Time A or Time B in Figure 9-1, depending on your position.</p>
<p class="ul">&bull; If you're west of Benghazi and closer to the aircraft carrier, the NAV cursor will point there&#8212;the NAV cursor will point to the closest friendly base. Either way, you're going to continue on to Suda Bay and land on the runway.</p>
<p class="ul">&bull; Once you're over the water, turn right to a heading of 050.</p>
<p class="ul">&bull; Climb to 4000 feet.</p>
<p class="ul">&bull; When you get closer to Suda Bay than the carrier, your HUD should look something like Time C in Figure 9-1. The NAV cursor is pointing toward Suda Bay, which is now to your northwest (to your left).</p>
<p class="ul">&bull; Continue on this heading and note how the NAV cursor moves to the left, toward the 000 indicator.</p>
<a id="page_153"></a>
<p class="p"><img src="153_1.jpg"/></p>
<em><b>Figure 9-1. Top of the HUD When Lining Up for a Landing</b></em>
<p class="ul">&bull; When the NAV cursor is one major division away from 000 (Time D in Figure 9-1), start a gentle turn to your left, toward the base. Your goal is to pull out of the turn with your wings level, just as the NAV cursor and the 000 indicator reach the center of the HUD (Time E).</p>
<p class="ul">&bull; Now you're heading straight toward the base on a heading of 000 and lined up for landing. If you've maintained your altitude<a id="page_154"></a> of 4000 feet, you should get a good look at the runway and be able to make any small adjustments necessary.</p>
<p class="ul">&bull; Now back off on the throttle until your airspeed falls below 300 and start your descent to the runway.</p>
<p class="ul">&bull; As soon as the wheels touch down, cut the engine power to minimum and hit the brakes button once.</p>
