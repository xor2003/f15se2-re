<h4 class="h4">Mission 7: The Persian Gulf</h4>
<p class="p"><b>Scenario:</b> Several times a year, the U.S. Navy conducts maneuvers in the Persian Gulf to show our determination to keep this vital shipping avenue open. This mission pits your F-15&#8212;flying on Combat Air Patrol&#8212;against attacking Iranian jets and missiles.</p>
<p class="p"><b>Tactics.</b> Since this is the final mission, you probably expect it to be the toughest. You won't be disappointed.</p>
<p class="p">There's no easy or best way to complete this mission. All the possible threats are waiting for you with expert-level operation. Skill and patience will be necessary to destroy all three primary targets and return safely to base. You'll probably take a hit or two in the process.</p>
<a id="page_146"></a>
<p class="p">Once hit, you should immediately determine the handling capabilities of the aircraft and return to base if necessary.</p>
<p class="p">This mission can be broken down into two, or possibly three, steps.</p>
<p class="p"><i>Part I.</i> The first flight is the optional one and consists of a Wild Weasel-style raid on one or two SAM locations on the western bank of the gulf. Destroying these targets will make the flight up to the northernmost target much safer, but will expose you to heavy missile fire during your bomb run and increase your exposure to Iranian aircraft also in the area. You'll have to make this decision: Do you want to complete the mission in two highly dangerous trips or three only slightly less dangerous steps?</p>
<ul type="bull">
<li><p class="p1">To fly this optional mission, first decide how many targets you intend to bomb: one or two. The southernmost SAM site on the western bank shouldn't pose a threat, so concentrate on the other two.</p></li>
<li><p class="p1">If you decide to attack only the middle SAM site, you can do this fairly safely by approaching low on afterburners, bombing, and then heading back to the ship on afterburners. If you intend to destroy both targets, you'll need to conserve fuel and use the afterburners only sparingly to avoid SAMs.</p></li>
</ul>
<p class="p">I recommend attacking the northernmost target first since it's the most difficult. It's very frustrating to fight your way in and out, destroying the first two targets, only to fail in your attempt on the third. By attacking in this order, you can rack up several bomb hits on each of the lower primary targets and increase your point total. If you get more than one hit on these targets on the first part of the mission and return to base, the mission will be over.</p>
<a id="page_147"></a><ul type="bull">
<li><p class="p1">To attack the northernmost target, fly up the gulf over water at medium altitude (at least 35,000 feet).</p></li>
<li><p class="p1">Proceed with only two bomb loads and at 85-percent power once you reach altitude. Fuel will be critical on this leg of the mission.</p></li>
<li><p class="p1">When you're slightly south of the target, cut in toward land, between the two SAM sites.</p></li>
<li><p class="p1">Use a steep descent to keep your speed over 1000 knots.</p></li>
<li><p class="p1">Make your bomb run (dropping both bombs), and return by the same path at high altitude on 85-percent power.</p></li>
<li><p class="p1">Remember to drop the external tanks as soon as you reach 13,500 pounds of fuel; the extra drag and added weight reduce your speed and fuel efficiency.</p></li>
</ul>
<p class="p"><i>Part II.</i> Once you've successfully completed the first part of your mission, the second part should be no problem.</p>
<ul type="bull">
<li><p class="p1">Set the NAV cursor on the first target and hit the afterburners.</p></li>
<li><p class="p1">Fly at about 3000 feet straight toward the target, cut back to 100-percent power once the target appears on medium-range radar, and make your bomb run.</p></li>
<li><p class="p1">If your first bomb misses and you can't accurately drop the second, don't go back; head toward the second target with the afterburners lit.</p></li>
<li><p class="p1">Repeat the process at the second target. Make your bomb run going in so that if you miss, you can return to the carrier. If you missed the first target, you can try again on your way back to base.</p></li>
<li><p class="p1">A total of four bomb loads is recommended for this leg of the mission: two for each target.</p></li>
</ul>
