<h1 class="h1">PART IV. SOFTWARE ORIENTATION FOR <i>F-15 STRIKE EAGLE II</i></h1>
<p class="p">The original version of <i>F-15 Strike Eagle</i> was much like the original version of the F-15 aircraft: hard hitting and exciting, but basic. As technology changed, the Air Force conducted a Multistage Improvement Program (MSIP) to bring the F-15 up to state of the art. Recently, MicroProse conducted its own MSIP to bring the <i>F-15 Strike Eagle</i> simulation up to the state of the art. Graphics, game play, and enemy intelligence have all been reworked to incorporate all the advances made since the original program was released in 1985.</p>
<p class="p">Although the game play is very similar, as is its overall &ldquo;feel,&rdquo; this simulation does operate differently and requires different tactics and strategies to be effective. This section will cover these tactics, information on game scoring, and detailed analysis of enemy capabilities and strategies for success in all the theaters of operations.</p>

