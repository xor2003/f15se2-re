<h4 class="h4">Mission 5: Hanoi, North Vietnam, 1972</h4>
<p class="p"><b>Scenario:</b> On 10 May 1972, United States aircraft first used laser-guided bombs in an attack on North Vietnam. These highly accurate devices could be guided to within several feet of a target, which was illuminated by a laser beam from an aircraft off the target.</p>
<p class="p">The <i>F-15 Strike Eagle</i> manual lists both primary targets as oil depots. One of the main targets for the 10 May strike, however, was the Paul Doumer Bridge, a major link in rail and road traffic in and out of Hanoi. A small strike force scored a decisive victory by landing 12 direct hits on the bridge with laser-guided bombs.</p>
<p class="p"><b>Tactics:</b> In this simulation, unlike the real event, SAMs won't be a factor. Since you don't have to worry about SAMs, the best approach is to take the targets one at a time. Go for the closest target first&#8212;climb to 10,000 feet, set the NAV cursor, and hit the afterburners.</p>
<p class="p">Drop all but two bomb loads to increase your speed and range. Fire Sparrow missiles at any enemy planes that come up against you, and keep heading straight for the target. You should be able to outrun the enemy planes if necessary.</p>
<p class="p">Fly directly over and past the target for about ten miles; then cut off the afterburners, perform a Split-S maneuver back toward the target, and make your bomb run, heading back toward base. Note that if both your bombs land on target, the mission counts as completed and you won't be able to go back out after the second target.</p>
<p class="p">To attack the second target, similar tactics are used. Follow a path north along the coast at 85-percent power, carrying two bombs. When you're almost even with the target, cut in and head toward it. Keep your power level at 85 percent as long as possible, but hit the afterburners once you're attacked. Go straight in for your bomb run, drop your bombs, light the burners, perform a vertical half-loop, and head straight for home. You should be able to outrun most of the enemy air traffic. Climb to 20,000 feet just in case you run out of fuel and need some altitude to coast back to the carrier.</p>
