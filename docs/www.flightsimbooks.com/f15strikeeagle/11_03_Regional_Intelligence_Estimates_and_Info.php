<h3 class="h3">Regional Intelligence Estimates and Info</h3>
<h4 class="h4">Libya</h4>
<p class="p1"><i><b>Reference</b>: Libya ONC map provided with the software package.</i></p>
<p class="ol"><b>1. MISSION:</b> You'll be assigned to make <i>deep interdiction</i> attacks on valuable targets well within the Libyan airspace and within it's air defense range. The three major areas of operation will be the area surrounding Tripoli, the oil field and equipment to the south of Port Brega, and the area surrounding Benina.</p>
<a id="page_183"></a>
<p class="ol"><b>2. ENEMY SITUATION:</b> An undeclared state of war exists with the government of Libya. Authorization has been given to attack all targets. Military and oil-producing targets are highest priority.</p>
<p class="ol2">A. Characteristics of the Area of Operations</p>
<p class="ol3">1. Military Geography: Topography</p>
<p class="ol3">a.&#160;<i>Situation:</i> Both corners of the Area of Operations are marked by mountainous areas (see the map)&#8212;the Jabal Nafusah on the Tripoli corner and the Al Jabal al Akhdar on the Al Bayda corner. Otherwise, the area is mostly hard-packed desert and very flat.</p>
<p class="ol3">b.&#160;<i>Effect on Enemy Capabilities:</i> Due to the flat nature of the land, radar and visual sighting ranges are generally very good, especially in the heavily defended area around the Gulf of Sirte. The exceptions are the two mountainous areas mentioned above.</p>
<p class="ol3">c.&#160;<i>Effect on Friendly Operations:</i> If missions call for operations beyond these mountainous areas, it's possible to avoid radar detection by sneaking around the corner, behind the mountain, and into Libyan territory.</p>
<p class="ol2">2. Military Geography: Hydrography</p>
<p class="ol3">a.&#160;<i>Situation:</i> Libya's coastline is dominated by the large Gulf of Sirte. The navigation rights and coastal waters claims regarding the gulf have been the source of much of the current conflict. This gulf lies in the center of the coastal area and is heavily defended both from the sea and air.</p>
<p class="ol3">b.&#160;<i>Effect on Enemy Capabilities:</i> Since the Gulf of Sirte is centrally located, it's easier to defend. SAM sites surround the gulf as do air bases. Intruders into the area can expect to be attacked from several different angles at once.</p>
<p class="ol3">c.&#160;<i>Effect on Friendly Operations:</i> The strength of the gulf defensive system lies on the corners at Benina and Misratah. The center of the gulf approach is usually guarded by a missile boat with air cover.</p>
<p class="p4">Depending upon the radar types used, it's possible to get very close to Libyan territory without radar detection by heading directly south from the carrier and either destroying or avoiding the boat and air patrols in that area.</p>
<a id="page_184"></a>
<p class="ol2">3. Military Geography: Climate and Weather Shouldn't be a factor.</p>
