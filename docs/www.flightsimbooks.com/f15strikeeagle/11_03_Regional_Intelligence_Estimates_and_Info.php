<p class="p3">Air-to-air weapons: All enemy aircraft can be equipped with early-version AIM-9 Sidewinder missiles. While they're good homers, they're also very short ranged and will frequently run out of fuel before reaching you. They can also be countered by flares. A few F-5s and F-14s may be fitted with early-version AIM-7 Sparrows. These can be countered with chaff and should be few and far between.</p>
<p class="p5">Your most dangerous opponent will be the F-14, due to its ability to detect and track you at long range. You won't face Doppler-guided air-to-air missiles in this region.</p>
<p class="ol3">b. <i>Naval Forces:</i> A number of military naval vessels will be seen in the area, but most of these are harmless. A few will carry Seacat SAMs, but these are relatively short ranged and are easily fooled with chaff alone.</p>
<a id="page_189"></a>
<p class="ol3">c. <i>Air Defenses:</i> The Iranian air defense system is again made up of outdated NATO components. The Hawk missile is the strongest of the bunch, but is a beamrider and is easily defeated with chaff. The Rapier is very fast, but also very short ranged, and is also defeated by chaff. A few Tigercat missiles may show up from time to time but are very easy to defeat and mostly ineffective.</p>
<p class="p3">You're lucky there are no Doppler-guided SAMs operating in this area. Anything fired at you can be fooled with chaff.</p>
<p class="ol3">2. Location</p>
<p class="ol3">a. <i>Air Bases:</i> Several bases line the coast, and another group is scattered in and beyond the mountains. The air bases provide good coverage and can launch a fighter to your position very quickly. You're rarely more than a minute or two away from enemy fighter coverage.</p>
<p class="ol3">b. <i>Naval Forces:</i> The armed patrol boats will generally stay close to important target areas, which means you should look for good coverage around Kharg Island, Bandar Khomenyi, and the Strait of Hormuz.</p>
<p class="ol3">c. <i>Air Defenses:</i> SAM coverage is widely spaced with strong coverage near Kharg Island, Bandar Khomenyi, and Bandar 'Abbas. SAMs also guard most airfields. Also, a large gap exists between Bushehr and Bandar 'Abbas, which you can exploit.</p>
<p class="ol2">C. Friendly Operations</p>
<p class="ol3">1. Established Airfields</br>Missions will generally start from either Ras Shaffaniyah in Saudi Arabia or Bahrain. Air strips that are close to enemy territory and available for emergency landings are Kuwait City, almost directly north of Ras Shaffaniyah (it may not show up on the display map in the cockpit) and Dubai, close to the Strait of Hormuz. These sites should be avoided if possible&#8212;both will frequently lie within range of enemy SAMs, which will make landing difficult for you.</p>
<p class="p2">The USS <i>Nimitz</i> is also just beyond the Strait<a id="page_190"></a> of Hormuz and can be used if necessary. Enemy aircraft won't normally follow you all the way across the gulf, making the landing sites there a little safer.</p>
<p class="ol3">2. Secret Airstrips</br> A secret rebel airstrip exists just to the northwest of Bushehr in the mountains. This strip shouldn't be attacked.</p>
<p class="ol3">3. Friendly Air Traffic</br> Several airports in the area have heavy civilian air traffic, especially Kuwait City. Take care when operating or fighting in these areas. Make sure you ID all targets before firing.</p>
<p class="ol2">D. Conclusions</p>
<p class="p3">The enemy in this area will, as a rule, perform poorly. They're equipped with inferior weapons and seem to lack the proper training and discipline. SAM systems aren't very effective, and enemy pilots seem to be very slow on the trigger. They seem to wait a long time before firing AAMs, which may give you several extra seconds for attacking ground targets, if needed. As a rule, the F-14s should be taken out first since they often function as AWACs aircraft, using their long-range radars.</p>
<h4 class="h4">Vietnam</h4>
<p class="p1"><b><i>Reference:</i></b> <i>Vietnam ONC map provided with the software package.</i></p>
<p class="p1"><b><i>Map Corrections:</i></b> <i>There are two SAM sites, three squares due east of Pnom Penh, which aren't shown on the map. Named</i> Ban Kheaand Ban Phlak, <i>they're roughly located at XU76. The SAM site and air base referred to on the map as</i> Wattay is <i>called</i> Viangchan <i>on the F-15 cockpit map display</i>.</p>
<p class="ol"><b>1. MISSION:</b> You'll be assigned to make <i>deep interdiction</i> attacks on valuable targets well within the Vietnamese airspace and within it's air defense range. The two major areas of operation will be the area surrounding Hanoi in the<a id="page_191"></a> north, and the lesser defended areas along the Ho Chi Minh Trail to the south.</p>
<p class="ol"><b>2. ENEMY SITUATION:</b> An undeclared state of war exists with the government of North Vietnam. Authorization has been given to attack all targets. Military, transportation (bridges), and supply dumps are highest priority.</p>
<p class="ol2">A. Characteristics of the Area of Operations</p>
<p class="ol3">1.Military Geography: Topography</p>
<p class="ol3">a. <i>Situation:</i> The heavily populated area to the north lies in a fairly flat area near the coast. All of the coastal areas are relatively flat and open, but once you cross beyond them, you'll find a large spine of mountains that run the length of the operating area. Beyond the mountains, the land becomes a flat delta plain.</p>
<p class="ol3">b. <i>Effect on Enemy Capabilities:</i> Due to the flat nature of the land near the coast, radar and visual sighting ranges are generally very good, especially in the heavily defended area around Hanoi.</p>
<p class="ol3">c. <i>Effect on Friendly Operations:</i> If missions call for operations beyond the mountainous areas, it's possible to break radar lock by flying at very low altitude once your beyond the mountain ranges.</p></li>
<p class="ol3">2. Military Geography: Hydrography</p>
<p class="ol3">a. <i>Situation:</i> Both North and South Vietnam are bordered by the Gulf of Tongking and the South China Sea. A number of rivers empty into the gulf near Hanoi. Bridges spanning these rivers are an important link in the local transportation network.</p>
<p class="ol3">b. <i>Effect on Enemy Capabilities:</i> Both countries have long coastlines that are difficult to defend.</p>
<p class="ol3">c. <i>Effect on Friendly Operations:</i> The hydrography in this area favors our operations. The large gulf makes an excellent area for carrier operations, and since the enemy has almost no naval forces, these carriers can operate in relative safety. Missions into the north and south can be launched from the carriers based in the gulf.</p>
