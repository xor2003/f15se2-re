<p class="ol3">b. <i>Naval Forces:</i> Enemy naval forces in this area are inconsequential.</p>
<p class="ol3">c. <i>Air Defenses:</i> The enemy Air Defense network has two basic components: the older component of SA-2 and SA-5 Soviet-made missiles, which are easily defeated with chaff, and the new sections of SA-12 SAMs, which are Doppler-guided and must be outmaneuvered. These newer SAMs are much more dangerous. As you move from easier to harder missions, the mix of missile types will shift from being mostly older to mostly newer.</p>
<p class="p5">SAM coverage is strongest near the coast. Once inland, the coverage is somewhat weaker. The least covered areas are in the Syrian Desert.</p>
<p class="p5">The SA-12 systems are few in number, but there will almost always be one located near the nuclear reactor sites in the northern region. The range of systems <a id="page_197"></a>will be dependent upon their locations. In any case, it's imperative that you assume a low altitude right after takeoff. Approaching the coast at 300 feet is recommended.</p>
<p class="ol3"> 2. Location</p>
<p class="ol3"> a. <i>Air Bases:</i> Most of the air strips are located in Syria. There are also bases near the Iraqi reactor sites.</p>
<p class="ol3"> b. <i>Air Defenses:</i> These forces are also mobile. While generally located in the same positions, the system used at that position will vary from mission to mission. When you first take off, it's best to check your display for the strongest signals, which usually indicate the Doppler SAMs. You can then choose to attack them or try to go around.</p>
<p class="p5">A new development in this area is the use of double SAM sites. The ONC map shows where the SAMs are located, but it doesn't show that the following locations operate <i>two</i> SAM systems at that location:</p>
<p class="p3">Baghdad</p>
<p class="p3">Damascus</p>
<p class="p3">Mosul</p>
<p class="p3">Kirkuk</p>
<p class="p3">Amman</p>
<p class="p3">Halab</p>
<p class="p4">Keep in mind that you'll need to use <i>two</i> AGMs to quiet the SAM activity at these sites.</p>
<p class="ol2">C. Friendly Operations</p>
<p class="ol5"> 1. Established Airfields</p>
<p class="p3"> The Malatya air base in Turkey will be the launch and recovery site for most missions in the north. When returning to the base, it's best to make your approach from the north, which will keep you out of range of the SAM sites in Syria.</p>
<p class="p5">The USS <i>Eisenhower</i> or a strip in Israel will usually be the launch point for missions in the south. Due to the heavy SAM coverage in the area, it's best to recover at Akrotiri air base on Cyprus. This will get you out of SAM range and allow for an unmolested landing.</p>
<a id="page_198"></a>
<p class="ol5">2. Secret Air Strips</p>
<p class="p3"> Rebel forces in enemy territory operate two secret airstrips just outside of Beirut. Take care when attacking targets in this area to avoid these two locations.</p>
<p class="ol3">D. Conclusions</br> The enemy in this area is generally well equipped. The major threat in the area comes from the Doppler-guided SAM systems, which are much more dangerous than the enemy aircraft. Enemy naval vessels shouldn't be a threat to your mission. For the best results at the Ace difficulty level, you should try to get missions with both the primary and secondary targets close together.</p>
<p class="p2">Good mission assignments might included attacking the bridge and reactor at Mosul, or a target in Damascus and Beirut. In both cases, the targets are close together, which means you only have to fight your way into one target area.</p>
<p class="p2">Safety is always located out to sea when operating in the south, and back to the west when flying in the north.</p>

