<p class="p5">Again, the most dangerous is the MiG-29 armed with AA-lOs. These Doppler-guided missiles can't be fooled with chaff alone. Escape maneuvering is also required.</p>
<p class="ol3">b.&#160; <i>Naval Forces:</i> The only part of the Libyan navy that can affect your mission is the section of Soviet-built Nunchucka II missile boats. These boats carry SA-N-4 SAM missiles, which are easily defeated with chaff. They also have a few other boats, but these have either poorer air defense capability or none at all.</p>
<a id="page_185"></a>
<p class="ol3">c. <i>Air Defenses:</i> The Libyan Air Defense network has two basic components: the older component of SA-2 and SA-5 Soviet-made missiles, which are easily defeated with chaff, and the new sections of SA-10 and SA-12 SAMs, which are Doppler-guided and must be outmaneuvered. The newer SAMs are much more dangerous. As you move from easier- to harder-level missions, the mix of missile types shifts from being mostly older to mostly newer.</p>
<p class="p2">SAM coverage is strongest near the coast. Once inland, the coverage is somewhat weaker. The least covered areas are the Cyrenaican Desert to the east of Port Brega, the Jabal Zaltan Oil Fields, and the rugged area to the south of Tripoli.</p>
<p class="p2">The range of these systems will depend upon their locations. In any case, it's imperative that you assume a low altitude right after takeoff. Approaching the coast at 300 feet is recommended.</p>
<p class="ol3">2. Location</p>
<p class="ol3"> a.&#160;<i>Air Bases:</i> Most of the air strips are located in the eastern portion of the country. Al Bayda, Benina, and Port Brega are all fairly close together and can launch aircraft in support of each other. The only major air base on the western coast is at Tripoli. If this base is destroyed, it will take a while for supporting aircraft to arrive from the east or from the Sabha air base to the south.</p>
<p class="ol3"> b.&#160;<i>Naval Forces:</i> By definition, these forces are mobile, but they can normally be found patrolling the gulf close to Port Brega and patrolling the likely approaches from Crete and Malta.</p>
<p class="ol3"> c.&#160;<i>Air Defenses:</i> These forces are also mobile. While generally located in the same positions, the system used at that position will vary from mission to mission. When you first take off, it's best to check your display for the strongest signals, which will usually indicate the Doppler SAMs. You can then choose to attack or try to maneuver around them.</p></li>
<a id="page_186"></a>
<p class="ol2">C. Friendly Operations.</p>
<p class="ol3"> 1. Established Airfields</br> Halfar on Malta, Suda Bay on Crete, and the Aircraft Carrier USS <i>America</i> are all good landing spots and are out of range of enemy SAM systems. The first two also have regular civilian traffic, so be sure to choose your targets carefully when fighting in these areas.</p>
<p class="ol3"> 2. Secret Air Strips</br> Secret forces in the area are currently operating two airfields. The Yafran air strip is to the south of Tripoli, and Al Mukhayli is to the east of Port Brega. Take care to avoid attacking these strips while attacking other ground targets in the area.</p>
<p class="ol2">D. Conclusions</p>
<p class="p3">The enemy in this area is generally poorly equipped. The major threat in the area comes from the Doppler-guided SAM systems: they're much more dangerous than the enemy aircraft. Enemy naval vessels shouldn't be a threat to your mission. It may be a good idea to avoid them, however, during your ingress to avoid detection as long as possible. Even if they can't attack, they can vector enemy aircraft in your direction. Enemy pilot missile-avoidance skills seem to be lacking, and most aircraft can be effectively engaged with head-on missile shots.</p>
<h4 class="h4">Persian Gulf</h4>
<p class="p1"><b><i>Reference:</i></b> <i>The ONC map provided with the software package.</i></p>
<p class="p1"><b>1. MISSION:</b> You'll be assigned to make quick, hard hitting attacks across the Persian Gulf. There will be three areas of operation: the northwestern end of the gulf around Bandar Khomenyi, the important oil terminal area around Kharg Island, and the gulf chokepoint near the Strait of Hormuz and Bandar-e Lengeh.</p>
<p class="p1"><b>2. ENEMY SITUATION:</b> The Iranian leadership has been advocating the overthrow of the American government for some time now in its efforts to export their religious revolution. They have declared that a state of war exists between<a id="page_187"></a> the two countries. Although this hasn't been made official by an act of Congress, all targets in Iran may be safely attacked. The most important of these are, of course, oil pumping and storage facilities and missile sites that might be used to attack shipping in the gulf.</p>
<p class="ol2">A. Characteristics of the Area of Operations</p>
<p class="ol3">1. Military Geography: Topography</p>
<p class="ol3">a. <i>Situation:</i> The coastal area is generally flat and open; few good hiding places exist close to the gulf. However, a large spine of mountains runs inland, parallel to the coast, which can provide cover once well inland.</p>
<p class="ol3">b. <i>Effect on Enemy Capabilities:</i> These mountains function to effectively limit the area covered by any one missile system, and they somewhat limit mutual support. SAM sites in the north end of the gulf can't detect low-flying aircraft beyond Bushehr, due to the mountains. Also, the inland SAM sites at Yazd and Esfahan are blocked and can't lock onto low-flying aircraft near the coast.</p>
<p class="ol3">c. <i>Effect on Friendly Operations:</i> Maintaining a low flight profile will limit the number of SAM sites that will be able to engage you at one time. Once inland, the rugged Zagros Mountains can be used for cover as well.</p>
<p class="ol3">2. Military Geography: Hydrography</p>
<p class="ol3">a. <i>Situation:</i> The main body of water in the area is obviously the Persian Gulf, which separates the safe areas to the south and west from the enemy areas to the north. A major waterway extends inland near the Strait of Hormuz in the southern region. This area has a number of important bridges that would make good military targets.</p>
<p class="ol3">b. <i>Effect on Enemy Capabilities:</i> The gulf provides a buffer for the enemy and provides a good field of view for radar operations. All aircraft attacking the enemy will have to cross the gulf through fairly good radar coverage. However, the gulf is long and narrow, and Iran is faced with having to defend a very long shoreline. Gaps in the radar coverage may exist, especially east of Kharg Island.</p>
<a id="page_188"></a>
<p class="ol3">c. <i>Effect on Friendly Operations:</i> Crossing the gulf is a given for almost any mission. Keep an eye out for gaps in radar coverage, but always maintain a low-altitude flight profile.</p>
<p class="ol3">3. Military Geography: Climate and Weather Shouldn't affect operations.</p>
<p class="ol2">B. Enemy Military Situation</p>
<p class="p">1. Composition</p>
<p class="p">a. <i>Air Forces:</i></p>
