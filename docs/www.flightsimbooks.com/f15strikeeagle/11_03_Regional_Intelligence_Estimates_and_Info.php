<p class="p3">Luckily, the enemy isn't armed with Doppler-guided air-to-air missiles, but the newer MiG-23s will be armed with both heat-seeking and radar-guided AA-6 missiles. It's important to verify the type of weapon being used before you employ countermeasures. The AA-2 heat-seekers used by the other aircraft are very short ranged. Wait until the missile is quite close before wasting a flare or making a maneuver&#8212;they'll frequently run out of propellant, especially if you're at high speed.</p>
<p class="ol3">b. <i>Naval Forces:</i> North Vietnam has no real navy to speak of, but it does have a number of small vessels that will stay close to shore. They aren't equipped with SAMs and shouldn't pose a threat.</p>
<p class="ol3">c. <i>Air Defenses:</i> The area around Hanoi and Haiphong in the north has one of the strongest air defense systems in the world. Extreme caution should be used when operating in this area.</p>
<p class="p3">Most of the SAMs encountered will be older SA-5s; a few SA-2 systems are also used. In a high-level difficulty<a id="page_193"></a> mission, a few Doppler-guided SA-12 systems will show up also. These are long-ranged and very dangerous.</p>
<p class="p5">The main SAM threat in this area comes from sheer numbers. The open coastal land area allows the SAM sites in the north to mutually support each other. Expect to find SAMs coming at you from every direction while in this area. It's best to decoy the SA-5s with chaff while taking out the more deadly SA-12s.</p>
<p class="p5">The area to the south has much lighter defenses, but the SAM sites in and around Pnom Penh in Cambodia will be troublesome for all aircraft operating out of the Ton San Nhut air base near Saigon. It's frequently a good idea to attack these sites immediately after takeoff to avoid problems later.</p></li>
<p class="ol4"> 2. Location</p>
<p class="ol4"> a. <i>Air Bases:</i> Most of the air strips are located in the northern portion of the country near Hanoi. Quite a few airfields are packed in fairly close together and can launch aircraft in support of each other. The major base in the south is across the border in Cambodia at Pnom Penh.</p>
<p class="ol4"> b. <i>Naval Forces:</i> These forces are few and far between and should pose no threat.</p>
<p class="ol4"> c. <i>Air Defenses:</i> These forces are also mobile. While generally located in the same positions, the system used at a position will vary from mission to mission. When you first take off, it's best to check your display for the strongest signals, which will usually indicate the Doppler SAMs. You can then choose to attack or try to go around them.</p>
<p class="ol2"> C. Friendly Operations</p>
<p class="ol5"> 1. Established Airfields</p>
<p class="p3"> You'll have no shortage of airfields to use in this region. Besides the two aircraft carriers in the gulf, there are safe strips on to the west in Thailand and all along the South Vietnamese coast.</p>
<p class="p5">Since there are so many bases in the area, you can adjust your attack plan to take off at one base and land at another. In the north, this might mean taking off from a carrier and attacking the target and then<a id="page_194"></a> continuing on into Thailand to land. In the south, you would take off from Tan San Nhut and attack your targets and then land at Nha Trang or Da Nang. This keeps you from having to cross back over into &#34;hot&#34; areas.</p>
<p class="p5">One friendly area to take note of is Udom air base in Thailand. This base is located very close to the North Vietnamese border and could be mistaken for an enemy base in the heat of battle.</p>
<p class="ol2">D. Conclusions</p>
<p class="p3">The enemy in this area is very poorly equipped but highly motivated. The major threat in the area comes from the Doppler-guided SA-12 SAM system and from the high quantity of SA-5 missiles. They're much more dangerous than the enemy aircraft. Enemy naval vessels shouldn't be a threat to your mission. Enemy pilot missile avoidance skills seem to be lacking, and most aircraft can be effectively engaged with head-on missile shots.</p>
<h4 class="h4"><b>Middle East</b></h4>
<p class="p1"><b><i>Reference:</i></b> <i>Middle East ONC map provided with the software package.</i></p>
<p class="p1"><b><i>Map Corrections:</i></b> <i>The air base and SAM site named</i> Palmyre <i>on the map is referred to as</i> Tadmur <i>on your cockpit map display.</i></p>
<p class="ol"><b>1. MISSION:</b> You'll be assigned to make <i>deep interdiction</i> attacks on valuable targets well within Lebanese, Syrian, Jordanian, and Iraqi airspace. The two major areas of operation will be the area surrounding Beirut and Damascus along the coast, and the northern areas of Mosul and Baghdad, where two nuclear reactors are located.</p>
<p class="ol"><b>2. ENEMY SITUATION:</b> An undeclared state of war exists with the governments in this area. Authorization has been given to attack all targets, but military and nuclear reactor targets are highest priority.</p>
<p class="ol2"> A. Characteristics of the Area of Operations</p>
<p class="ol3"> 1. Military Geography: Topography</p>
<p class="ol3">a. <i>Situation:</i> The entire coastal area is marked by high <a id="page_195"></a>mountains. Otherwise, the area is mostly hard packed desert and very flat.</p>
<p class="ol3">b. <i>Effect on Enemy Capabilities:</i> Due to the mountainous nature of the land near the coast, radar and visual sighting ranges aren't optimum. Mutual support isn't always possible due to the mountains blocking radar coverage.</p>
<p class="ol3">c. <i>Effect on Friendly Operations:</i> The mountains near the coast will help block enemy radar signals during your approach. Maintaining a low flight profile in this area will prevent other SAM sights located farther away from locking onto your position. Once beyond the mountains, the area is very open and flat, making it possible for the enemy to detect you at long range.</p>
<p class="ol3">2. Military Geography: Hydrography</p>
<p class="ol3">a. <i>Situation:</i> The area's coastline with the Mediterranean is rather short, but much of the population is located near the coast.</p>
<p class="ol3">b. <i>Effect on Enemy Capabilities:</i> The small coastal area of Lebanon and Syria is easily and heavily defended. Good radar coverage out to sea makes an unobserved approach very difficult.</p>
<p class="ol3">c. <i>Effect on Friendly Operations:</i> Our navy has complete control of the Mediterranean. The USS <i>Eisenhower</i> aircraft carrier is located just off shore and is a well-located launching point for flights against targets near the coast.</p>
<p class="ol3">3. Climate and Weather Shouldn't be a factor.</p>
