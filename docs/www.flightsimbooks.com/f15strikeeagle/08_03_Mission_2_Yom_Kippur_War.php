<h4 class="h4">Mission 2: Yom Kippur War, 1973</h4>
<p class="p"><b>Scenario:</b> In October 1973, hostilities between Egypt and Israel again reached the boiling point. On 6 October 1973, Egyptian tanks crossed the Suez Canal. At the same time, 100 Syrian tanks moved into the Golan Heights area of Israel. During the Yom Kippur War, many large-scale air battles were fought between Israeli F-4 Phantoms and Syrian and Egyptian MiG-21s and MiG-17s. The Israeli Air Force (IAF) estimated a kill ratio of about 40 to 1. The Israeli F-4s downed 164 Egyptian aircraft in air combat while losing four. On the Syrian front, meanwhile, three Israeli jets were lost while destroying 95 Syrian aircraft.</p>
<p class="p"><b>Tactics:</b> Take advantage of the limited capabilities of the Egyptian SA-7 SAMs. Flying above 32,000 feet puts you out of SAM range; using afterburners lets you outrun any air resistance on the way in.</p>
<p class="p">Go toward the primary target at high altitude and high speed, pass over the target, and begin your descent. Turn back toward the target, approach at 2000-3000 feet, drop as many bombs as possible in your single run, and continue on toward the base at high speed and altitude.</p>

