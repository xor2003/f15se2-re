<h3 class="h3"><b>The Fighter Pilot's Mission</b></h3>
<p class="p1">The jet fighter pilot's mission is to intercept, engage, outmaneuver, out-gun, and eliminate enemy aircraft. That's it, pure and simple. But as part of this team, you'll also be asked to strike enemy positions well behind the <i>forward edge of the battle area</i> (FEBA). Although you may be flying the hottest plane in the sky, remember your mission first. Once you've eliminated your assigned targets, you're free to go MiG hunting.</p>
<p class="p">Any true fighter pilot will tell you there are two kinds of aircraft: <i>fighters</i> and <i>targets.</i> An aircraft that has anything to do with delivering air-to-ground weapons (bombs) is a target. Heavy bomb loads reduce maneuverability and speed as well as cause your aircraft to gulp fuel more rapidly. Therefore, it's in your best interest to hit your assigned targets quickly so you can get rid of your weapons load. With your load lightened, you become a very dangerous weapon, ready to go looking for trouble.</p>
