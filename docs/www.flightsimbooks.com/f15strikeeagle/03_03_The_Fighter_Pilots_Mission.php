<p class="p">Fighters sent up just to wait for trouble generally fly what's known as Combat Air Patrol (CAP). Fighters flying a CAP are configured strictly for air-to-air combat. F-15s on CAP usually carry Sidewinder short-range missiles, Sparrow mediumrange missiles, and a Gatling gun or cannon. But as a member of this team, you won't have to wait around hoping that trouble stumbles into your path. You're sent out-looking for and causing your own trouble and action. If you don't relish the idea of testing yourself against the best the enemy has to offer, maybe you're in the wrong business.</p>

