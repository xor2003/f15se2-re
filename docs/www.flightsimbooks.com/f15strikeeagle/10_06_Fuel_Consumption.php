<h3 class="h3"><b>Fuel Consumption</b></h3>
<p class="p1">There's no doubt about it, the F-15 can suck up some jet fuel. Quite often, the success or failure of your mission will depend on your fuel state, so fuel managementis important right from takeoff.</p>
<p class="p">The main thing that affects your fuel consumption rate is the throttle setting. Flying around at full military throttle will eat up your gas in a hurry, and using your afterburner will burn gas even faster. The trick is in knowing when to use full power&#8212;you must be patient. Don't be in such a hurry to get to the target area. Go in at 60-percent throttle or so; the fuel you save may save your life later.</p>
<p class="p">Altitude also effects fuel consumption, but since most of your flying will be at low levels (below 10,000 feet), this doesn't really concern you. Climb to high altitudes to conserve fuel and you'll attract SAMs from all over the map.</p>
<p class="p">One good thing about being low on fuel is that your top speed increases. A full fuel tank will knock about 100 knots off your top speed.</p>

