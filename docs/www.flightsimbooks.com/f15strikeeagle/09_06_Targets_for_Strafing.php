<h3 class="h3"><b>Targets for Strafing</b></h3>
<p class="p1">Your cannon is effective against most ground targets; only very hard targets such as runways can't be destroyed with it. Depots are especially good strafing targets since they're often located away from other targets and they don't shoot back. Oil tanks also go up in a hurry under a gun attack.</p>
<p class="p">Although it may be dangerous, SAM sites can make good gun targets. If you approach from the correct angle, you can destroy a missile launcher or two as well as the radar in one pass. Once over the site, you're pretty safe since the SAM launchers can't shoot straight up.</p>
<h4 class="h4"><b>Air-to-Air Missiles</b></h4>
<p class="p1">The air-to-air missiles used in this simulation have great range and are very accurate. In fact, the ranges and accuracy are probably better than you realize. The program has these ranges fixed at the following:</p>
<h4 class="h4"><b>AMRAAM</b></h4>
<p class="p1">Max range: 36km</p>
<p class="p1">Max effective range: 16km</p>
<h4 class="h4"><b>Sidewinder</b></h4>
<p class="p1">Max range: 18km</p>
<p class="p1">Max effective range: 9km</p>
<p class="p1">with the <i>max range</i> being the farthest away you can be and still get a hit (where the target box changes to an oval), and the <i>max effective range</i> being the high-probability kill zone (where the oval changes color).</p>
<p class="p">These ranges actually vary a great deal, depending on your speed, the target's speed, and your position relative to the target. In other words, if you're on afterburners and the target is coming at you head-on, your speed and the target's speed are both effectively added to the speed of the missile, allowing it to be fired at a target much farther away than normal. In this situation, you could be reasonably sure of a hit by firing an AMRAAM when the target is still 50km away&#8212;long before the target box turns to an oval. In fact, it's a good idea to shoot early at head-on targets. If you wait until the oval changes color, the target may be by you before the missile has a chance to track it and lock on.</p>
<a id="page_161"></a>
<p class="p">Conversely, if the target is flying away from you at high speed, and you're flying very slow, an AMRAAM fired from 30km might not reach the target. However, missiles fired from the rear are harder to jink.</p>
<p class="p">Shots fired at the side of a target, especially Sidewinders, have a lower probability of hitting. In a close-in fight, it's better to wait until you're behind the target to fire. Shooting at a target flying across your path is often a waste of a good missile, especially at close range.</p>
<p class="p">Great care should be taken when firing air-to-air missiles near a friendly base. The skies are often crowded with friendly aircraft as well as the bad guys. Unfortunately, your missile can't tell the difference. If a friendly aircraft should happen to wander in front of an enemy, the missile would lock onto him and probably land you in hot water.</p>
<h4 class="h4"><b>Air-to-Ground Missiles</b></h4>
<p class="p1">Using the Maverick missile on ground targets really couldn't be simpler. In fact, its probably the one area of <i>F-15 II</i> that's actually easier and less complex than the original version. There's little aiming required: just fire it off in the general direction. However, waiting for the range to close is very important. If you fire before the oval changes color, your chances of scoring an effective hit go down. Therefore, if at all possible, wait for the oval to change colors before letting your Maverick shot fly, especially when attacking important or dangerous targets.</p>
<p class="p">There are times when, for whatever reason, you must fire early&#8212;in fact, the earlier the better. Firing at high speed does give your Maverick a bit of range extension, but not as much as with the air missiles. Firing at speeds over 800 knots will give you about five extra kilometers, increasing the max range from 30 to 35 klicks.</p>
<h4 class="h4"><b>Use All Your Weapons</b></h4>
<p class="p1">There's no use in carrying a weapon all the way into enemy territory and then bringing it back home again. Use all your weapons every time out. If fuel isn't a problem, you can go looking for more air or ground targets. If it looks like fuel may be a problem, make sure you use all your ground attack weapons&#8212;at least while in the target area. It might be a good idea<a id="page_162"></a> to keep at least one air-to-air missile with you until you're over safer ground, but try to find a target for that one on your way home as well.</p>
<h4 class="h4"><b>Multiple Bogeys</b></h4>
<p class="p1">No two ways about it: You'll almost always find yourself up against more than one enemy aircraft. The trick is to deal with as many as possible at long range. Try to even the odds before getting into a close-in dogfight.</p>
<p class="p">Since you can only track one target at a time, its best to start shooting at close-to-max range if more than one target is in front of you. Let an AMRAAM fly early; then switch to Sidewinders. By the time the AMRAAM hits its target, you'll probably be in Sidewinder range.</p>
<p class="p">You can attack more than one air target at a time if they're off in different directions. If you have one target to your left and one to your right, you can fire at one and then swing to face the other one. The HUD should then be targeting it. Then, you can launch a second missile. This can only be done if the targets are far enough apart so they both don't appear on the HUD at the same time.</p>
<p class="p">Whenever you're engaged in a multiple bogey fight, it's a good idea to keep your speed and energy level up. Use high-speed slashing attacks, and change targets often. Concentrating too much on one target will allow others to swing around and get on your tail. Always try to keep them off balance.</p>
<p class="p">Also remember to keep an eye out for SAMs in this situation. When things start to get crowded on your radar display, it's easy to miss an incoming SAM.</p>

