<h3 class="h3"><b>ACM Checkride &#35;3</b></h3>
<p class="p1"><i>This ride will let you practice getting out of bad situations.</i></p>
<p class="p1"><b>Simulation setup:</b> The setup is the same for both versions of <i>F-15 Strike Eagle</i> unless otherwise stated.</p>
<p class="p1"><b>Level:</b> Pilot</p>
<p class="p1"><b>Mission:</b> Libya</p>
<p class="p1"><b>Objective:</b> To reverse a defensive situation into an offensive situation and destroy the bogey.</p>
<p class="p1"><b>Procedure:</b> Start the simulation and take off. A MiG should appear close by. Continue to fly straight and climb to 10,000 feet. Allow the bogey to follow you and come around on your tail. Now use one of the defensive maneuvers to attempt to reverse the situation. Repeat this drill, each time allowing the bogey to get closer to your tail before attempting to escape and attack.</p>

