<a id="page_47"></a>
<p class="p"><b>Nose-to-tail turn.</b> In a nose-to-tail turn, you turn toward your opponent at the time you pass.</p>
<p class="p"><img src="047_1.jpg"/></p>
<em><b>Figure 3-6. Nose-to-Tail Turn</b><br/>The aircraft with the best turning performance will be in the shooting position.</em>
<p class="p">This puts you in a turning contest. You're chasing the bogey's tail and he's chasing yours. All things equal, the plane with the best turning performance will eventually catch up with the other and be in perfect position for a gun or short-range missile shot. However, pilots are rarely equal&#8212;just ask one. A clever pilot who knows how to manage his energy during the turning battle can frequently outmaneuver a lesser pilot in a more maneuverable aircraft. F-4 victories against the smaller and more maneuverable MiG-17s in Vietnam proved this.</p>
<a id="page_48"></a>
<h4 class="h4"><b>Pursuit Options</b></h4>
<p class="p1">The path you take when following a target is referred to as a <i>pursuit curve.</i> There are three kinds of pursuit curves: <i>Lag, Pure,</i> and <i>Lead</i>. Most rookies instinctively use Pure pursuit, in which they point the nose of their plane directly at the bad guy. This approach is okay, but seasoned pros know that Lead and Lag pursuit offer distinct advantages. Pursuit curves generally refer to one thing: where to point the nose of the aircraft. Following a Lead pursuit curve&#8212;keeping your nose ahead of the bad guy&#8212;assures that you can &ldquo;pull lead&rdquo; on the target for a good guns shot.</p>
<p class="p">Here's why pulling lead is so important. If you're in a turning fight with another aircraft and you have your nose pointed directly at him, despite what you may think, he's perfectly safe. You see, it takes a certain amount of time for the shells you fire to travel the distance between you and the target. By the time the shells get there, the target will be gone. It's just like throwing a football to a running receiver: You don't throw it to where the receiver is, you throw it to where he <i>will be</i> by the time the ball gets there. The same thing applies to gunnery: You must shoot where the target will be, not where it is.</p>
<a id="page_49"></a>
<p class="p"><img src="049_1.jpg"/></p>
<em><b>Figure 3-7. Pursuit Curves</b></em>
<a id="page_50"></a>
<p class="p">When you're following a target in a lead-pursuit curve, you have lead on that target. In other words, your nose is pointing in front of the target, not at it. This allows you to shoot where the target will be, not where it is. If you get into an engagement using lead pursuit from the beginning, you won't have to out-turn your opponent. Rather than flying straight at him, use the Tactical display or the TrackCam to see which way he's heading or turning; then approach him with your nose pointed in front of him, not at him.</p>
