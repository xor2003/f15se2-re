<h3 class="h3"><b>Air Combat Basics</b></h3>
<p class="p1">The aerial chess game of move and countermove, one plane against another, has been a difficult thing for experts to pin down over the years. The rules are constantlychanging; what worked yesterday may not work tomorrow, or the latest technology may be defeated by an obsolete system in actual combat conditions.</p>
<p class="p">Regardless of the level of sophistication of the equipment, however, the most important measure of a country's air power is how well the pilots can tactically maneuver to utilize and deliver the weapons available. It has been shown over and over that flying skills and aggressiveness can overcome more recently developed weapons and superior numbers.</p>
<p class="p">Despite all these changes, certain basic principles have remained constant over the years. The effectiveness of an attack is basically determined by four criteria, which are listed here in order of priority.</p>
<h4 class="h4"><b>Surprise</b></h4>
<p class="p1">Although surprise isn't often possible with the F-15 Strike Eagle simulation, it can be accomplished by firing medium-range missiles early, as soon as an enemy aircraft is detected coming toward you.</p>
<p class="p">Surprise can also occasionally be gained by detecting an enemy with long-range radar and making a wide, slow turn to position yourself behind the opponent. Once in the favorable position, accelerate and close fast.</p>
