<h4 class="h4"><b>Teamwork</b></h4>
<p class="p1">Teamwork is the second most important factor. Unfortunately, you're all by yourself when flying your F-15 Strike Eagle simulator. You'll be responsible for carrying out your assigned mission totally by yourself, no wingman to watch your six. (However, two-person play with one flying the plane and the other operating the keyboard can greatly improve your chances for survival.)</p>
<h4 class="h4"><b>Maneuverability</b></h4>
<p class="p1">Historically, only one out of five kills has been made by outmaneuvering an opponent, but when you're involved in a low-speed turning dogfight, outmaneuvering suddenly becomes very important. Develop and practice your skills.</p>
<h4 class="h4"><b>Weapons</b></h4>
<p class="p1">As the lethality of weapons used in air-to-air combat has increased, the targets have become harder to hit due to their increased speed and maneuverability. All things considered, the current close-range missiles aren't much deadlier than the machine guns used during World Wars I and II.</p>
<h4 class="h4"><b>Aerial Combat</b></h4>
<p class="p1">Aerial combat can be broken down into five stages:</p>
<p class="ul">&bull; Detection</p>
<p class="ul">&bull; Closing</p>
<p class="ul">&bull; Attack</p>
<p class="ul">&bull; Maneuvering</p>
<p class="ul">&bull; Disengagement</p>
<p class="p">Although in actual practice some of these phases may be skipped, each needs to be examined and understood.</p>
<a id="page_42"></a>
<h4 class="h4"><b>Detection</b></h4>
<p class="p1">The earlier you detect the position and course of an enemy aircraft, the more time you'll have to form a battle plan and maneuver to a favorable position to attack or avoid.</p>
