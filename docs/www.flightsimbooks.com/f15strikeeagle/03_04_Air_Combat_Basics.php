<h4 class="h4"><b>Closing</b></h4>
<p class="p1">Once an enemy has been detected, you must make a decision whether or not to close. You must evaluate the damage condition of your airplane, the amount of fuel you have remaining, your current mission, and your weapons remaining in order to make this decision. In some cases, it's best to continue a bombing run or head for home.</p>
<p class="p">If you do choose to attack, you should close as quickly as possible. Your direction of approach will primarily depend on the enemy's path in relation to yours. If the enemy aircraft is flying across your path or away from you, try to get close behind it as quickly as possible, before it has a chance to turn toward you.</p>
<p class="p">If the enemy plane is coming directly at you, you can try for lateral separation for a turn back. If he won't let you, you must close head-on.</p>
<h4 class="h4"><b>Attack</b></h4>
<p class="p1">Getting off the first shot is important. With an F-15, this usually means a medium-range missile shot followed by an attempt to get behind the enemy plane to follow up with a Sidewinder missile or guns. Your best move is to attack from behind. If that isn't possible, the head-on approach is your next choice.</p>
<p class="p">You must also decide which weapon to use. Medium-range missiles may be out of the question at this point due to the minimum-range requirements. If Sidewinders are chosen, care should be taken not to get too close during the attack. If the first-shot attack isn't successful, you'll move on to the next phase of air combat.</p>
<a id="page_43"></a>
<h4 class="h4"><b>Maneuvering</b></h4>
<p class="p1">If your long-range or rear surprise attack isn't successful, you must then try to outmaneuver your opponent. Decide beforehand what type of weapons you want to attack with and what type of flight plan you'll follow.</p>
<p class="p">If you're at low altitude (below 2000 feet), a close-range turning fight is going to be tough. The energy you lose during this kind of flight can put your aircraft in a stall. You can quickly find yourself out of altitude, out of energy, and out of ideas. At low altitudes, it's best to keep your speed up.</p>
<p class="p">At higher altitudes, a close-range, turning, gun attack is possible. Your first move for this type of fight is to cut your power to 75 or 80 percent, which gives you the best turning performance. You may need to increase power during a sustained turn to avoid stalling. Keeping your speed low also keeps the enemy in front of you&#8212;you're less likely to overshoot.</p>
<h4 class="h4"><b>Turning Options</b></h4>
<p class="p1">Once you decide to engage a bogey from head-on, you have a number of options when it comes to your opening move.</p>
<p class="p"><b>Lead turn.</b> To perform this maneuver, put some lateral separation between yourself and your opponent; then turn early (before your opponent) toward the target. This will place you in an advantageous position behind the enemy.</p>
