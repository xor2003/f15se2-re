<p class="p">Lead pursuit will also allow you to close the range on a faster moving target by letting you &ldquo;take a short-cut&rdquo; and cut across the turning circle.</p>
<p class="p">Lag-pursuit, or keeping your nose pointed <i>behind</i> the target, can be effectively used when you're trying to track a maneuvering target from the rear. In this case, trying to pull lead on the target may get you into trouble. What can happen is this: As you pull hard on the turn to get your nose ahead of the target, he reverses course and catches you going the wrong way. By keeping your nose pointed behind the target, he'll have to pass through your gunsights if he reverses course, giving you the chance for a quick guns shot. By maintaining a Lag-pursuit position, you'll also be able to maintain a speed advantage over the enemy. If he doesn't reverse, you may be able to force him into an ever-tightening turn. Sooner or later, he'll run out of airspeed or altitude and be forced to make a straight-line run for it. Then, you can use your speed advantage to close for a gun shot or to line him up for a missile.</p>
<h4 class="h4"><b>Lateral Separation</b></h4>
<p class="p1">Use lateral separation combined with lead pursuit to attack those unsuspecting targets. Putting a little space between your flight path and the bogey's will make your turn and attack much easier. That way, you won't have to turn and race after him, and you'll be able to keep your nose in front of the target<a id="page_51"></a> the entire time. You can either maintain the lead pursuit until you close the range for a gun shot, or slide in behind him and put a Sidewinder up his tailpipe.</p>
<p class="p">Maneuvering after the initial pass is discussed in detail later.</p>
