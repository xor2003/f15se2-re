<h4 class="h4"><b>Disengagement</b></h4>
<p class="p1">In the F-15 simulation, there's no real disengagement. The enemy fighters continue to follow you all the way back to the base, or until they're shot down. There are times, though, when it's necessary to put space between you and your opponent, to regroup or take a missile shot. These maneuvers are covered in detail in Chapter 9.</p>
<p class="p">In <i>F-15 Strike Eagle,</i> as it has been historically, the best way to disengage from an enemy fighter is to shoot it down.</p>
<h4 class="h4"><b>Situational Awareness</b></h4>
<p class="p1">The term <i>situational awareness</i> became hot during and after the war in Vietnam, but the concept has been around since World War I. However, the concept is a difficult one to grasp. In general, situational awareness (SA) refers to a pilot's ability to keep up with what's going on in a rapidly changing environment. Not only must he keep track of the situation, he must also be able to use that information to predict what's about to happen.</p>
<p class="p">Good SA requires you to think and feel in three dimensions and to be able to understand and keep track of various groups of time, distance, and relative motion as they apply to your aircraft and those around you.</p>
<p class="p">As difficult as SA is to describe, it's even more difficult to teach. Experience helps, but not all experienced pilots have good SA. Some say that SA is more like a talent than a skill&#8212;hard work can improve it somewhat, but if you weren't born with it, there's little you can do to get it.</p>
<p class="p">Stories abound from all the wars about pilots with great SA. They're the ones who finished off their targets in wild dogfights and then got on the radio to complement one of their squadron mates on <i>his</i> success. Their excellent SA allowed them to win their own engagements and keep up with several other dogfights simultaneously!</p>
<p class="p">Oswald Boelcke was one of the great pilots of World War I. While he possessed great SA, he quickly realized that most of the new pilots being trained and sent to his squadron for combat possessed none at all. When friend and competitor Max Immelmann was shot down, Boelcke wrote a series of rules for air combat. While obviously intended for the novice, these contain a good deal of useful information applicable even in today's world of Mach-2 fighters and Mach-4 missiles.</p>
<a id="page_53"></a>
<p class="p1">Many of the rules deal with improving your own SA and reducing the enemy's.</p>
<h4 class="h4"><b>Oswald Boelcke and the Eight Rules of Air Combat</b></h4>
<p class="p1">Air combat has changed dramatically since World War I. Basic tactics and rules established by Oswald Boelcke in 1916, however, have stood the test of time well. These are eight rules of air combat designed to help new pilots survive and win.</p>
<p class="ol"><b>1. Try to secure an advantage before you begin your attack.</b> This advantage can be altitude, position, or surprise.</p>
<p class="ol"><b>2. Always carry through with an attack once you've started.</b></p>
<p class="ol"><b>3. Fire only at close range and when your opponent is properly in your sights.</b> When applied to missile attacks, this means shoot when in good position with a good angle and within the minimum and maximum firing ranges. When applied to guns, it means fill the screen with the enemy plane.</p>
<p class="ol"><b>4. Always keep your eye on your opponent. Don't be deceived by ruses.</b> In <i>F-15,</i> this means be prepared for your opponent to cut his speed to force you to overshoot.</p>
<p class="ol"><b>5. Always attack from behind your opponent.</b> This isn't as important now as it was in 1916, although it still applies to a good gun attack.</p>
<p class="ol"><b>6. When attacked, turn into the attack; don't try to evade.</b> This still holds true. Always be on the offensive.</p>
<p class="ol"><b>7. When over enemy lines, never forget your own line of retreat.</b> When your fuel and/or weapons are low, start thinking about how you're going to get back to base.</p>
<p class="ol"><b>8. Attack in groups of four or six.</b> Unfortunately, you're by yourself in <i>F-15.</i></p>
<p class="p">This is the end of the ground training and familiarization section. Study this section and be prepared for anything the enemy can throw at you.</p>
