<h4 class="h4">Two-Player Team</h4>
<p class="p1">Although the <i>F-15 Strike Eagle</i> manual makes only slight mention of the two-player system, I feel it's very important. The real F-15E is a two-man aircraft, as is the Navy's F-14 Tomcat. The benefit isn't only in the extra pair of hands for operating complex weapons systems, it's also in the extra pair of eyes. These same benefits translate well to the simulation.</p>
<p class="p">In a two-player simulation, one person operates the joystick, functioning as pilot, and the other operates the keyboard controls, functioning as weapons officer, or .GIB (Guy In Back), as they're referred to. The pilot doesn't have to look away from the screen to push buttons or check radar.</p>
<p class="p">In this system, duties should be divided as they're outlined below.</p>
<p class="p"><b>Pilot's responsibilities:</b></p>
<ul type="bull">
<li><p class="p1">Fly the airplane.</p></li>
<li><p class="p1">Choose and release weapons.</p></li>
<li><p class="p1">Give orders (someone must have the final say).</p></li>
</ul>
<p class="p"><b>Weapons officer's responsibilities:</b></p>
<ul type="bull">
<li><p class="p1">Watch the radar screen. Things happen very quickly in battle and the radar screen gives you the best clues to what's coming at you.</p></li>
<li><p class="p1">Watch the radar and infrared-scanning indicator lights. The warning lights tell you what type of countermeasures to use. When two SAMs are in the air, the weapons officer must determine which is the infrared-homing SAM and which is<a id="page_138"></a> radar-guided. This can only be done by noticing which warning light comes on first.</p></li>
<li><p class="p1">Deploy countermeasures and tell the pilot to break away and in what direction.</p></li>
<li><p class="p1">Place the NiW indicator in the correct position.</p></li>
<li><p class="p1">Drop the external fuel tanks when the fuel level reaches 13,500 pounds. Continuing to fly with the drop tanks attached reduces your fuel efficiency and performance.</p></li>
<li><p class="p1">Respond to pilot commands to operate the following systems: speedbrake, throttle increase or decrease, afterburners, arming weapons, bail out, rear-view control, reminding the pilot of altitude during combat.</p></li>
</ul>
<p class="p">It's important to give the second player as much to do as possible to keep him or her from getting bored and becoming a spectator instead of a participant.</p>

