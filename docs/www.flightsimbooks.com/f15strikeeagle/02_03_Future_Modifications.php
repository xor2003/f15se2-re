<h3 class="h3"><b>Future Modifications</b></h3>
<p class="p1">A 20-percent power increase is planned for 1991 when the F-15E will start to receive the new General Electric and Pratt &#38; Whitney engines. This <i>increased performance engine</i> program (IPE) will up the power rating of each engine to 29,000 pounds of thrust.</p>
<p class="p">Also in the works is a short takeoff and landing version of the F-15. This experimental aircraft will be able to take off or land on a bomb-damaged runway only 50 feet wide and 1500 feet long, in a 30-knot crosswind, with no active ground-based radar guidance.</p>
<a id="page_28"></a>
<p class="p"><img src="028_1.jpg"/></p>
<em><b>Figure 2-8</b><br/>A cutaway view of the new F-15S/MTDshort takeoff and landing/maneuver technology demonstrator.</em>
<p class="p">This aircraft is fitted with forward canard wings and thrust-vectoring and thrust-reversing engine nozzles. Also included is a new Autonomous Landing Guidance system that will assist the pilot in a number of areas, including setting the glidepath for landing and flare-maneuver guidance.</p>
