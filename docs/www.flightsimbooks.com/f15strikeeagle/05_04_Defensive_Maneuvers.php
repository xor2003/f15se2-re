<h3 class="h3"><b>Defensive Maneuvers</b></h3>
<p class="p1">This section offers you several defensive options you can use to turn the tables in those tight spots. Don't be afraid to use the pause feature of <i>F-15 Strike Eagle</i> to review your options if you find yourself in trouble.</p>
<p class="p">Attempt various escape techniques, and try to find one or two you feel most comfortable with. Practice and perfect those maneuvers for the best results.</p>
<h4 class="h4"><b>High-G Barrel Roll</b></h4>
<p class="p1"><b>Situation:</b> An enemy aircraft is close behind, closing for a gun attack.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Break hard into the attack (1).</p>
<p class="ul">&bull; Perform a Barrel Roll in the opposite direction. This could bleed off some speed and make the attacker overshoot (2).</p>
<p class="ul">&bull; Roll in behind the target for a gun or short-range missile shot (3).</p>
<a id="page_93"></a>
<p class="p"><img src="093_1.jpg"/></p>
<em><b>Do a Barrel Roll away from the enemy; then roll back behind him for a tailpipe shot.</b></em>
<a id="page_94"></a>
<h4 class="h4"><b>Disengagement</b></h4>
<p class="p1"><b>Situation:</b> You're in a low-speed, hard-turning fight and can't gain an advantage after several turns. Sometimes the best maneuver is to disengage and reposition to attack again under better conditions.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Roll inverted (1).</p>
<p class="ul">&bull; Pull back on the stick to start a steep dive (2).</p>
<p class="ul">&bull; Keep the stick pulled back until you pull out right-side up but heading in the opposite direction (3).</p>
<p class="p">Timing has a lot to do with the success of your disengagement attempt. If possible, make your move in one direction when the bogey is headed in the other.</p>
<a id="page_95"></a>
<p class="p"><img src="095_1.jpg"/></p>
<em><b>Roll upside-down and put your fighter into a steep dive. You'll end up with a heading 180 degrees from your original.</b></em>
<a id="page_96"></a>
<h4 class="h4"><b>Speedbrake Reversal</b></h4>
<p class="p1"><b>Situation:</b> An attacker is closing fast from behind.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Level out your wings (1).</p>
<p class="ul">&bull; Cut your engines, pull up hard, and extend your speedbrake (2).</p>
<p class="ul">&bull; As soon as the attacker appears on your screen (overshoots), retract the speedbrake and be prepared to turn hard to follow, or take a missile shot and disengage.</p>
<p class="p">You must be quick to react on this one; the bogey won't like being out in front of you and will turn hard very quickly. You'll only have a good shot for a split second.</p>
