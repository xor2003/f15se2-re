<h4 class="h4"><b>Defensive Spiral Dive</b></h4>
<p class="p1"><b>Situation:</b> An enemy aircraft has closed to within firing distance behind you. You have at least 15,000 feet of altitude, and you want to (a) disrupt the enemy's aiming process, (b) escape, and (c) if possible, reverse the situation.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Roll to an inverted position (1).</p>
<p class="ul">&bull; Pull back on the stick to start the dive (2).</p>
<p class="ul">&bull; Determine the position of the attacker (3).</p>
<p class="ul">&bull; Begin rolling toward the attacker. In other words, if he's to your left, push the stick to the left (4).</p>
<p class="ul">&bull; As your speed builds, cut power to idle and extend your airbrake.</p>
<p class="ul">&bull; If the attacker follows you down, he may overshoot at this point and allow you to take a shot.</p>
<a id="page_99"></a>
<p class="p"><img src="099_1.jpg"/></p>
<em><b>There's a chance the attacker may overshoot If you use this splraling maneuver.</b></em>
<a id="page_100"></a>
<h4 class="h4"><b>Vertical Spiral</b></h4>
<p class="p1"><b>Situation:</b> You're being out-turned by an enemy aircraft. Your opponent is pulling around behind you. The Vertical Spiral lets you take advantage of the excellent design of your aircraft&#8212;the F-15 can climb while it's in a hard turn.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Pull the nose up to a 30-degree climb (1).</p>
<p class="ul">&bull; Roll into the attack and light your afterburners. Hold this climbing turn until you've gained 5,000-10,000 feet or so in altitude. Most air-to-air missiles fired at you will be from behind during this maneuver and can be defeated with a properly timed flare (2).</p>
<p class="ul">&bull; Cut your afterburners. Locate the enemy and take a missile shot (3). Depending upon the quality of the enemy aircraft, he may or may not be able to follow you up. Also your F-15 may have a higher ceiling (max altitude) than some of the older enemy aircraft, allowing you to gain an advantage even if they try to follow you up.</p>
<a id="page_101"></a>
<p class="p"><img src="101_1.jpg"/></p>
<p class="p">Climb and turn at the same time; then locate the target and fire or dive.</p>

