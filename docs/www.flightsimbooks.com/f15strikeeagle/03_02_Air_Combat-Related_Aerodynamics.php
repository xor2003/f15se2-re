<p class="p">Another method of increasing acceleration is to use a very steep dive. During a steep or <i>ballistic dive</i>, gravity greatly increases your aircraft's acceleration. In this case, if two planes are equal in all aspects but weight, the heavier plane will accelerate faster and achieve a higher terminal velocity.</p>
<p class="p">If two planes are equal in weight but one has lower drag (because its shape is more efficient, or it's carrying fewer externals such as drop tanks or bombs), the one with the lower drag will have the acceleration advantage.</p>
<p class="p">Even in this kind of steep or ballistic dive, it's best to perform the unloading maneuver first, and then progress to steeper dive angles.</p>
<a id="page_36"></a>
<h4 class="h4"><b>Turning Performance</b></h4>
<p class="p1">One of the most important performance characteristics of a modern fighter plane is its ability to turn sharply and to maintain a tight turn for an extended period of time. In most contexts, maneuverability and turn performance are synonymous. The better an aircraft's turn performance, the better it maneuvers. That, of course, translates into a better chance of winning a fighter/fighter contest.</p>
<p class="p">Turn performance is generally divided into two types: instantaneous turn performance and sustained turn performance.</p>
<p class="p"><i>Instantaneous turn performance</i> is an aircraft's ability to turn at any given point in time. This is a function of the aircraft's speed and altitude. As the term implies, the turn doesn't have to be sustained for more than an instant. Something called <i>maximum instantaneous turn performance</i> is achieved at very high speeds. Altitude is also a factor here since as you get higher, the density of air is reduced. The reduced amount of air passing over the wings reduces lift capability, which then reduces the turning performance.</p>
<p class="p"><i>Sustained turn performance</i> is the aircraft's ability to maintain a turn for an extended period of time. Turn performance is measured three ways:</p>
<ul>
<li><p class="p1"><i>Load factor</i> or G's (gravity units) pulled during the turn. In a 5-G turn, the pilot weighs five times as much as normal.</p></li>
<li><p class="p1"><i>Turn radius,</i> which is the area it takes to accomplish a complete turn. This is normally expressed in feet or miles.</p></li>
<li><p class="p1"><i>Turn rate,</i> which is how fast the aircraft is changing course during a level turn. This is expressed in degrees of change per second.</p></li>
</ul>
<p class="p">The maximum G force an aircraft can handle is set by the manufacturer and normally allows for a significant safety margin. High-G turns can be performed at low and high speeds, but keep in mind that any time you're performing a maximum-G turn, all available lift is used just to maintain the current altitude. If you must climb, you'll have to reduce the angle and severity of the turn.</p>
<p class="p">However, the aircraft's maximum-G turning ability isn't the most important factor to a fighter pilot. Turn rate and turn radius are more important because they determine the ability<a id="page_37"></a> of the aircraft to turn inside another plane, either to escape or to obtain the necessary lead angle for a shot.</p>
<p class="p">Maximum turn rate and minimum turn radius can best be obtained in high-G low-speed turns. Normally the speed in these turns is just slightly higher than the stall speed for that aircraft. When turning this slow at maximum G, it becomes imperative that you watch your airspeed. A stall will send you rapidly toward the ground and disrupt any maneuver you're attempting. Once your airspeed begins to bleed off in these turns, you must decrease the G load in order to increase airspeed and prevent a stall.</p>
