<a id="page_33"></a>
<p class="p">A good rule of thumb is to maintain a high cruise speed (Mach .9 or so) and a good cushion of altitude (35,000 feet or so) when entering a combat situation. With this speed and altitude, you'll have all the options of climbing or diving at will. Just remember, this altitude can put you in a bad situation tactically on some missions, so the optimum situation isn't always appropriate.</p>
<h4 class="h4"><b>Climb Performance</b></h4>
<p class="p1">Your aircraft's ability to gain altitude, or <i>climb,</i> is hampered by weight and drag. The easiest way to improve your climb performance is to get rid of any unnecessary equipment. If you're going to concentrate on air-to-air combat, get rid of any bombs you're carrying. All that air-to-mud equipment only slows you down, making you more vulnerable.</p>
<p class="p">If you have external fuel tanks that are empty, drop them, too (in the F-15 Strike Eagle, for instance, your external tanks are empty when fuel remaining is less than 13,500 pounds). Fuel tanks increase drag, hurting your ability to climb.</p>
<p class="p">The F-15 has excellent climb performance when not loaded down with bombs and fuel. In this configuration, the F-15 is considered <i>ballistic</i> because it can produce more pounds of thrust than the plane weighs and therefore can accelerate straight up at full power.</p>
<p class="p">For each aircraft, there's an optimum climb rate at which trading speed for altitude and altitude for speed is minimized. When performing a sustained climb, keep your airspeed in mind. If you climb too steeply, you'll use fuel faster and lose air speed, which will take some time to recover once you reach your desired altitude. If your climb is too shallow, it will take longer to reach your desired altitude.</p>
<h4 class="h4"><b>Acceleration Performance and Unloading</b></h4>
<p class="p1">Acceleration is primarily affected by weight and thrust. Kicking in the afterburner increases your speed somewhat, but the best way to pick up a lot of speed in a short amount of time is to dive.</p>
