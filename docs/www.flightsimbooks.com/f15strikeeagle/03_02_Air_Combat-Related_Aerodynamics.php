<a id="page_35"></a>
<p class="p">The trick here isn't to push hard on the stick in an attempt to dive straight down, but to perform a maneuver called <i>unloading</i>. In unloading, you're removing the weight of the aircraft that slows acceleration.</p>
<p class="p">To do this, push forward on the stick slightly to obtain a gentle dive. This starts a gradual dive and produces a <i>zero</i> G <i>condition</i>. This is similar to going over a small hill with a car or bicycle&#8212;as you go over the crest, you're momentarily weightless as you come out of your seat. The same thing happens when you unload an aircraft; however, an aircraft can continue to lose altitude and thus remain unloaded for quite some time.</p>
<p class="p">Without the weight of the airplane holding it back, the speed of the plane can be increased rapidly. This can happen so quickly, in fact, that you need to keep your eye on the air-speed indicator to make sure you don't exceed the structural design limits of the aircraft, or <i>Vmax</i>. The Vmax of an aircraft is the maximum airspeed it can attain without ripping its wings off.</p>
<p class="p">The F-15 Strike Eagle simulator gives a visual warning when you're approaching Vmax.</p>
<box>
