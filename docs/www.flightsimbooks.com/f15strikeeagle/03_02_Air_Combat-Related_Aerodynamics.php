<p class="p">Care should also be taken when you're increasing thrust or decreasing G's during a turning fight. This can easily cause you to overshoot your opponent and quickly change your posture from an offensive one to a defensive one.</p>
<h4 class="h4"><b>Roll Performance</b></h4>
<p class="p1"><i>Roll performance</i> is the ability of the aircraft to change its plane (geometrically speaking) of operation&#8212;its ability to go from level flight to inverted flight or into a steep bank maneuver.</p>
<p class="p"><i>Roll acceleration</i> determines how fast an aircraft can get into a steep banking maneuver or a continuous roll, and is a good measure of the aircraft's &ldquo;agility.&rdquo; The aircraft that rolls the fastest has the advantage during a close turning fight.</p>
<p class="p">Roll performance and roll acceleration are basically determined by the design of the aircraft, although roll acceleration can be increased by unloading the aircraft before performing the roll. Be sure to level out the plane after the roll to prevent an excessive loss of altitude.</p>
<a id="page_38"></a>
<h4 class="h4"><b>Inverted Flight Performance</b></h4>
<p class="p1">The F-15 has excellent inverted (upside down) flight performance characteristics. It's very steady and can even climb while inverted. Sustained inverted flight is rarely necessary in combat situations, but brief periods of inverted flight are necessary to perform many of the offensive and defensive maneuvers used in air combat.</p>
<p class="p">Developing good inverted flight skills takes time and practice. All controls are reversed when you're flying upside down. To fly toward the ground, for instance, you pull back on the stick; to go up, you push forward. To execute a bank to the right, you must push the stick to the left. These skills should be practiced and perfected so you don't make the mistake of flying into trouble rather than away from it.</p>
<p class="p">Jet aircraft wings are very efficient and provide a great amount of lift. When you need to dive in a hurry, this lift capability can be used to pull you down toward the ground by flying inverted and pulling back on the stick. This is an oftenused escape maneuver. You not only dive quickly, but when you pull out, you're heading 180 degrees from your original course.</p>

