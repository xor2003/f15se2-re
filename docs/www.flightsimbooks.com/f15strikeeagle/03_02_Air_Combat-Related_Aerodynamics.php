<h3 class="h3"><b>Air Combat-Related Aerodynamics</b></h3>
<p class="p1">This section serves as a refresher course in aerodynamics as they apply to flying your aircraft in combat. It isn't intended to be a complete course, just an overview of information that will enable you to understand the physics behind some of the maneuvers used in air combat, and how to use them to your advantage.</p>
<h4 class="h4"><b>Energy Maneuverability</b></h4>
<p class="p1">The concept of energy awareness during air combat is fairly new. Wise use and conservation of energy during combat will increase your chances of victory.</p>
<p class="p">Your aircraft has two kinds of energy: <i>kinetic</i> and <i>potential.</i> Kinetic energy is related to airspeed. High levels of kinetic energy, or speed, are needed to perform many combat maneuvers. Potential energy is related to altitude and the force of gravity on your aircraft. If you have low speed (kinetic energy) but high altitude (potential energy), you can dive and pick up speed needed to perform a series of combat maneuvers. Conversely, if you have high speed but low altitude, you can convert this speed (kinetic energy) into altitude (potential energy) by climbing.</p>
<p class="p">This combination of airspeed and altitude is often referred to as your <i>energy state.</i> The aircraft's ability to climb, dive, and accelerate to change this state is called <i>energy maneuverability.</i> So what does this mean to you in combat situations? To illustrate this concept, consider the following examples.</p>
<a id="page_32"></a>
<p class="p"><b>Example 1.</b> You're at low altitude, down in the weeds, approaching an enemy aircraft at the same altitude head-on. You're flying considerably faster than your opponent. In a tight turning fight, the slower plane will have the advantage, but you're thinking in three dimensions. So as you approach, you pull up into a steep climb and your opponent pulls up after you. This is called a <i>zoom maneuver.</i></p>
<p class="p">Since you possess more kinetic energy (you're flying faster), you're able to climb higher and gain the advantage. He'll run out of airspeed first and be forced to dive to regain it. You can then pitch back and dive to get on his tail.</p>
<p class="p"><b>Example 2.</b> You're following an enemy aircraft flying at the same speed as you, but you're at a higher altitude. As your enemy twists and turns in an effort to escape, he'll lose speed (kinetic energy). If you follow him through those turns, you'll lose speed, too. But, because you're at a higher altitude, you'll have more potential energy, so you can dive to pick up speed, catch the enemy, and maneuver into a good firing position.</p>
<p class="p">What these examples mean to you, a fighter pilot, is that you must constantly keep an eye on your speed and altitude during combat. A heavy-handed pilot who twists and turns the aircraft around without paying attention to energy losses will soon be unable to maneuver. Once lost, energy is hard to regain; then your only hope is to dive hard and regain some speed (assuming, of course, you've left yourself enough altitude to perform this maneuver.) The lesson here is to avoid low altitude and low speed conditions. If you don't, you're a sitting duck for air-to-air or surface-to-air fire. Also, in this type of fight, it's easy to depart the flight envelope and stall. At low altitude, a stall generally translates into a smoking hole in the ground.</p>
