<h3 class="h3"><b>The DRF (Dual-Role Fighter) Program</b></h3>
<p class="p1">After the development of the F-15 for air combat missions, the USAF still needed a replacement for the aging F-111. The Air Force sought a <i>dual-role fighter</i> (DRF)&#8212;one that could not only mix it up in air-to-air combat, but could also carry a substantial bomb load in day or night and in all weather. Ironically, the very characteristics that made the F-15 an excellent fighter also made it a contender for the DRF program.</p>
<p class="p">Multistage improvement programs began for both the F-15 and F-16 aircraft. Modifying the F-16 to perform this dual role required a new wing design, while F-15 modifications were made more in the area of avionics and airframe strengthening. Based on the cost of the programs, the F-15 was chosen, and on 24 February 1984, the Air Force Chief of Staff approved $1.5 billion for upgrading 392 F-15s to perform the dual-role fighter mission. The F-15 DRF was designated the <i>Strike Eagle.</i></p>
<h4 class="h4"><b>F-15E Fact Sheet</b></h4>
<p class="p1"><b>Designer and Manufacturer:</b> McDonnell Douglas Corp., St. Louis.</p>
<p class="p1"><b>Power Plants:</b> Two Pratt &#38; Whitney F-100-PW-220 low-bypass engines, each producing 24,000 pounds of thrust.</p>
<p class="p1"><b>Avionics:</b></p>
<p class="ul">&bull; Hughes APG-70 synthetic aperture radar. Employs high-, medium-, and low-pulse repetition frequency for optimum all-aspect detection and lock-on. Also features high-resolution ground mapping modes.</p>
<a id="page_16"></a><p class="ul">&bull; IBM Central Computer</p>
<p class="ul">&bull; Kaiser wide-view HUD</p>
<p class="ul">&bull; Honeywell ring-laser-gyro inertial navigation system</p>
<p class="ul">&bull; Seven Sperry multipurpose color and monochrome display screens</p>
<p class="p1"><b>Length:</b> 63.3 feet<br/><b>Height:</b> 18.5 feet<br/><b>Wingspan:</b> 42.8 feet<br/><b>Max Gross Takeoff Weight:</b> 81,000 pounds<br/><b>Speed:</b> Mach 2.5 plus<br/><b>Load Factor:</b> &#8212;3g to + 9g</p>
<p class="p"><img src="016_1.jpg"/></p>
<em><b>Figure 2-1. F-15E Strike Eagle</b><br/>The F-15E Strike Eagle has a two-man crew, advanced display systems, and the ability to carry a wide range of air-to-air and air-to-ground weapons.</em>
<p class="p">The F-15 Strike Eagle is a two-man aircraft, with many changes to the cockpit. This redesign allows a new wide field-of-vision head-up display, automated navigation, and a series of nondedicated screens that can be used for displaying moving area maps, weapons choices, radar mapping, and FLIR (Forward-Looking InfraRed). These screens can be used for<a id="page_17"></a> targeting weapons and navigation. Software enhancements to the existing radar system allow the use of high-resolution radar ground maps and target detection up to 100 nautical miles away.</p>
<p class="p"><img src="017_1.jpg"/></p>
<em><b><i>Figure 2-2. F-15 Strike Eagle</i> Screens</b><br/>This is a view of the front (top) and rear (bottom) seats of the F-15E Strike Eagle displays and screens. Note that this is a simulator.</em>
<a id="page_18"></a>
<p class="p">This new F-15E Strike Eagle can deliver a weapons payload similar to that of an F-111 and can defend itself from air and ground attack in the process&#8212;something the heavier F-111 can't do.</p>
<h4 class="h4"><b>Targeting/Navigation Systems</b></h4>
<p class="p1">The new Hughes APG-70 Synthetic Aperture radar is the heart and soul of the Strike Eagle's navigation and targeting system. It can provide a high-resolution image at much greater distances than previous radar setups (exact range CLASSIFIED).</p>
<p class="p">To counter the threat of radar emissions being detected by the enemy, the APG-70 doesn't operate continuously. In a high-threat situation, such as a deep interdiction mission behind enemy lines, the radar comes on briefly and makes a quick sweep. It then processes the return image into a photoquality freeze frame of the area ahead. It scans a 90-degree arc in front of the aircraft. Once the target is located and the weapons officer has a clear picture of it, the target image and location are stored in the weapon system memory and the radar can be shut down, greatly reducing the chances of being located and targeted by enemy SAM systems and AAA (AntiAircraft Artillery).</p>
<p class="p">Night and inclement weather offer good cover to avoid detection by one of the best devices on the battlefield, the Mark 1 Human Eyeball. The <i>low-altitude navigation and targeting infrared for night</i> system (LANTIRN) lets this Eagle hunt when most other systems are blind. The LANTIRN system allows the F-15E to deliver guided and unguided weapons as accurately at night and in bad weather as an F-16 can in daylight. This system is contained in two 500-pound pods: one for targeting and one for navigation. Both attach under the wings beneath the engine intakes.</p>
<a id="page_19"></a>
<p class="p"><img src="019_1.jpg"/></p>
<em><b>Figure 2-3. F-15 Targeting and Navigation Equipment</b><br/>The two LANTIRN pods shown under the F-15E allow it to perform deep interdiction missions in bad weather or at night.</em>
<p class="p">The navigation pod contains the sensor for the FLIR (Forward-Looking InfraRed) system. This system produces TV-quality video images that are processed and projected directly onto the pilot's HUD (Head-Up Display) and to one of the WSO's (Weapons Systems Officer) CRT displays. The FLIR allows both crewmen to &ldquo;see&rdquo; the upcoming terrain almost as clearly as they could in daylight. This pod also contains the low-altitude terrain-following radar, which can be coupled directly to the flight controls for hands-off &ldquo;in the weeds&rdquo; flights down to 200 feet.</p>
<p class="p">The targeting pod of the LANTIRN system contains another FLIR for target tracking and a laser target designator. Once the target area is reached, the targeting FLIR sweeps the area quickly to make a crisp freeze-frame image. The pilot or the WSO can then target any item in the image by placing a cursor on it. This information is processed and downloaded to the weapons, which can use it directly, such as the AGM-65 Maverick, or to the laser designator, which can guide smart bombs to the target. With enough altitude, targets can be designated from ten miles away. Maverick missiles can be deployed from even farther distances.</p>
<a id="page_20"></a>
<h4 class="h4"><b>Weapons Systems</b></h4>
<p class="p1">With only slight modifications, the F-15 can deliver an extraordinary range of weapons:</p>
<p class="ul">&bull; AIM-7M Sparrow medium-range, semiactive radar air-to-air missiles</p>
<p class="ul">&bull; AIM-9L Sidewinder infrared-homing air-to-air missiles</p>
<p class="ul">&bull; AIM-120 Advanced medium-range air-to-air missiles</p>
<p class="ul">&bull; M61 20mm six-barrel gun</p>
<p class="ul">&bull; AGM-88A Harm antiradar missiles</p>
<p class="ul">&bull; AGM-65A Maverick TV-guided air-to-ground missiles</p>
<p class="ul">&bull; AGM-65D IIR infrared imagery missiles</p>
<p class="ul">&bull; AGM-65C Laser-guided air-to-ground missiles</p>
<p class="ul">&bull; AGM-84A Harpoon antiship missiles</p>
<p class="ul">&bull; MK20 Rockeye bombs on multiple ejection racks</p>
<p class="ul">&bull; Matra Durandal runway denial weapons</p>
<p class="ul">&bull; MK82 500-pound bombs in <i>Slick</i> (low drag) and <i>Snakeye</i> (retarded flight) configurations</p>
<p class="ul">&bull; MK84 2000-pound bombs in Slick, laser-guided, infrared-homing, and electro-optical versions</p>
<p class="ul">&bull; GBU-12 Paveway laser-guided bombs</p>
<p class="ul">&bull; GBU-24 Laser-guided bombs</p>
<p class="ul">&bull; GBU-15 Laser-guided glide bombs</p>
<p class="ul">&bull; Mk 20 Rockeye cluster bomb units</p>
<p class="ul">&bull; GE 30mm gun pods</p>
<p class="ul">&bull; Tactical nuclear weapons</p>
<p class="ul">&bull; ASAT antisatellite missiles</p>
<h4 class="h4"><b>Air-to-Ground</b></h4>
<p class="p">As you can see, the F-15 Strike Eagle is equipped to handle a wide variety of air-to-ground weapons, including laser-guided glide and free-fall bombs. For the purposes of your mission, however, the most important is the AGM-65 Maverick missile.</p>
<h4 class="h4"><b>AGM-65 Maverick</b></h4>
<p class="p1"><b>Manufacturer:</b> Hughes Aircraft</p>
<p class="p1"><b>Propulsion:</b> Thiokol solid rocket motor (reduced smoke)</p>
<p class="p1"><b>Size:</b> 98 inches long, 12 inches in diameter, 28-inch wing span</p>
<p class="p1"><b>Weight:</b> 460&#8211;700 pounds at launch, depending upon the warhead attached</p>
<p class="p1"><b>Speed:</b> Mach 1.2</p>
<p class="p1"><b>Simulation:</b> <i>F-15 Strike Eagle II</i></p>
<a id="page_21"></a>
<p class="p"><img src="021_1.jpg"/></p>
<em><b>Figure 2-4. The AGM-65 Maverick Missile</b></em>
<p class="p">Hughes Aircraft won the contract in 1968 and continues to be the prime contractor. The original A version was a formidable weapon, and the system has been greatly improved over the years. The version you'll be using is the AGM-65D with an infrared seeker. Specially designed for use with the LANTIRN system, this version provides for maximum standoff capability and improved accuracy in rain and fog, and at night.</p>
<p class="p">The A version uses the larger 250-pound blast/frag warhead as opposed to the original 130-pound shaped charge design. This allows the Maverick to be effective against small ships as well as land-based targets. Also, the Air Force has been investigating placing Tactical Nuclear Warheads on AGM-65 missiles.</p>
<p class="p">Once the target is designated and locked, the missile is totally fire-and-forget. As soon as the missile is launched, the pilot is free to maneuver to avoid enemy fire or leave the area. No further guidance is needed.</p>
<a id="page_22"></a>
<h4 class="h4"><b>Air-to-Air</b></h4>
<p class="p1">The air superiority role should be very familiar to any F-15 pilot, as should the weapons involved. For air-to-air, the F-15E uses the standard Air Force combination of radar-guided weapons for medium range, and infrared seekers for short range. In this case, the AIM-9 Sidewinder, AIM-7 Sparrow, and AIM-120 AMRAAM missiles are carried.</p>
<h4 class="h4"><b>AIM-9 Sidewinder</b></h4>
<p class="p1"><b>Manufacturer:</b> Versions 9L and 9P, Ford Aerospace; versions 9L and 9M, Raytheon</p>
<p class="p1"><b>Propulsion:</b> Various manufacturer's solid rocket motor, reduced smoke</p>
<p class="p1"><b>Size:</b> Approximately 120 inches long, 22-inch span, depending upon version</p>
<p class="p1"><b>Weight:</b> From 172 to 190 pounds, depending upon version</p>
<p class="p1"><b>Mission Time:</b> 40-60 seconds for newer versions</p>
<p class="p1"><b>Simulation:</b> <i>F-15 Strike Eagle I and II</i></p>
<p class="p"><img src="022_1.jpg"/></p>
<em><b>Figure 2-5. The AIM-9 Sidewinder Missile</b></em>
<a id="page_23"></a>
<p class="p">Conceived by the Naval Weapons Center, China Lake, in the 1950s, the Sidewinder has become one of the best and most influential missiles in history. The original version was the model of simplicity&#8212;it was said to have &ldquo;only 24 moving parts, and fewer electronic components than the average radio.&rdquo; This, of course, also meant low cost.</p>
<p class="p">The modern Sidewinder is much more complex, but also much better. The breakthrough came with the 9L version, which has <i>all-aspect capability</i> (ALASCA). No longer must the missile be fired from behind the target at the hot tailpipe. The 9L, version has a seeker that can pick up heat from air friction along the leading edges of the bogey's wings and engine intakes. This head-on capability has given the U.S. a large advantage over the Soviets, who are just now developing allaspect IR missiles.</p>
<p class="p">Once the bogey is targeted and locked, the Sidewinder is a totally fire-and-forget missile. No further guidance is needed. The Sidewinder does tend to lock onto the nearest target, so care should be taken in crowded skies.</p>
<h4 class="h4"><b>AIM-7 Sparrow</b></h4>
<p class="p1"><b>Manufacturer:</b> Raytheon</p>
<p class="p1"><b>Propulsion:</b> Solid rocket motor</p>
<p class="p1"><b>Size:</b> 144 inches long, 8 inches in diameter, and a span of 40 inches</p>
<p class="p1"><b>Weight:</b> At launch, 452 or 502 pounds, depending upon the version</p>
<p class="p1"><b>Speed:</b> Mach 4</p>
<p class="p1"><b>Range:</b> Older versions, 28 miles; newer versions, 62 miles</p>
<p class="p1"><b>Warhead:</b> 88 pounds</p>
<p class="p1"><b>Simulation:</b><i>F-15 Strike Eagle I</i></p>
<a id="page_24"></a>
<p class="p"><img src="024_1.jpg"/></p>
<em><b>Figure 2-6. The AIM-7 Sparrow Missile</b></em>
<p class="p">One of the largest U.S. air-to-air missiles, the <i>Sparrow,</i> was designed for medium beyond-visual-range use. It uses SARH (SemiActive Radar Homing) guidance, which requires that the target be &ldquo;illuminated&rdquo; by radar from the fighter all through the flight. As long as the source fighter isn't forced to make wild maneuvers, it can illuminate the target. However, the problem comes when the fighter comes under enemy fire while the missile is in flight. Often, the fighter must maneuver to avoid a SAM launch, which can cause it to lose radar lock on the target.</p>
<p class="p">This problem led to the development of the AIM-120 AMRAAM, which is similar in size and range but is a fire-and-forget type weapon.</p>
<h4 class="h4"><b>AIM-120 AMRAAM</b></h4>
<p class="p1"><b>Manufacturer:</b> Hughes Aircraft</p>
<p class="p1"><b>Propulsion:</b> Solid rocket motor</p>
<p class="p1"><b>Size:</b> 145 inches long, 7 inches in diameter</p>
<p class="p1"><b>Weight:</b> At launch, 363 pounds</p>
<p class="p1"><b>Speed:</b> Mach 4+</p>
<p class="p1"><b>Warhead:</b> Approximately 50 pounds</p>
<p class="p1"><b>Simulation:</b> <i>F-15 Strike Eagle II</i></p>
<a id="page_25"></a>
<p class="p"><img src="025_1.jpg"/></p>
<em><b>Figure 2-7. The AIM-120 AMRAAM</b></em>
<p class="p">The AMRAAM (Advanced Medium-Range Air-to-Air Missile) was developed to replace the aging AIM-7 Sparrow series. Designed to be somewhat smaller, cheaper, and more reliable, the AMRAAM may eventually prove to be as popular and effective as the famed Sidewinder series.</p>
<p class="p">It has advanced sensors that can detect a target at extreme range. Once launched, it doesn't need further guidance or target illumination by the firing aircraft, which frees the fighter to clear the area, concentrate on a second target, or maneuver for a second shot at the first target. The AMRAAM is undoubtedly the finest medium-range missile in operation. It has very good maneuverability and sophisticated electronics to prevent it from being fooled by enemy countermeasures.</p>
<h4 class="h4"><b>Weapons in Recent Combat</b></h4>
<p class="ul">&bull; In 1981, two Libyan SU-22 Soviet-made fighters were downed by Sidewinders fired from U.S. Navy F-14 Tomcats.</p>
<p class="ul">&bull; In 1982 in a series of massive dogfights over Lebanon's Bekaa Valley, the Israeli Air Force&#8212;flying F-15s and F-16s&#8212;shot down 85 Syrian jets plus a number of helicopters. Again, Sidewinders and Sparrows were used extensively.</p>
<a id="page_26"></a>
<p class="ul">&bull; Later in 1982, 16 Argentine fighters were shot down by British Harriers using Sidewinders.</p>
<p class="ul">&bull; The most recent incident was January 1989, when two U.S. Navy F-14 Tomcats were threatened by two Lybian MiG-23 Floggers. In a classic head-on confrontation, both MiGs were shot down.</p>
<p class="p">When two early head-on Sparrow missiles missed, the F-14s split up. Both MiGs followed the wingman F-14 who pitched back into the MiGs and downed one with a second Sparrow shot. The lead F-14 turned back and closed in on the tail of the other MiG in a perfect bracket maneuver. Seconds later, he splashed the second Flogger with a Sidewinder shot.</p>
<p class="p">Clearly, these weapons work and are effective. Of the two, the Sidewinder has been credited with many more kills than the Sparrow, the reasons being twofold.</p>
<p class="ul">&bull; First is the problem of identification of targets beyond visible range, an area in which the Sparrow is most effective. Visual identification of enemy aircraft is the current order of the day in air combat to prevent shooting down a member of your own air force. Consequently, most jets are too close for Sparrows by the time they're identified.</p>
<p class="ul">&bull; The second problem lies in the fact that Sparrows rely on semiactive radar detection for homing in on the enemy, which means that once the missile is fired, the F-15 must continue flying toward the target to bounce radar signals off it for the missile to follow. In combat with multiple opponents, it isn't always possible to concentrate on just one target for any length of time. The Sidewinder, on the other hand, is a fire-and-forget weapon. Once it's launched, the pilot is free to evade or attack.</p>
<p class="p">The good news is that neither of these problems will plague you when flying missions for the Eagle Rapid Deployment Team; identification is rarely a problem. You're by yourself over enemy territory most of the time, so you can safely assume that any other aircraft are unfriendly. This isn't the case in F-15 <i>Strike Eagle II</i> however; many friendly aircraft may be in the area around friendly bases. The Sparrow missiles used in F-15 <i>Strike Eagle 1</i> don't require attention once they're<a id="page_27"></a> launched. (Remember, you can't fire another missile until the previous one completes its flight.) Also, <i>F-15 Strike Eagle II</i> uses the AMRAAM missile, which is truly fire-and-forget.</p>
<h4 class="h4"><b>F-15E Delivery and Competition</b></h4>
<p class="p1">The first Air Force squadron to go operational with the F-15E was the 336th Tactical Fighter Squadron at Seymour Johnson Air Force Base in North Carolina. The future of the dual-role fighter, however, is in doubt since funding for the program was cut and production is set to stop in 1991. This will provide the Air Force with only 200 aircraft out of the original order for 392.</p>
<p class="p">The main fear seems to be one of obsolescence. Many feel that even aircraft capable of operating at low altitude and high speed will no longer be safe from enemy SAMs in the near future, and that Low Observability Technology is the way to go. Also, the Air Force does have an aircraft in development&#8212;the ATF (Advanced Tactical Fighter)&#8212;that will incorporate stealth technology as a means of defense.</p>
<p class="p">Unfortunately, In this age of budget cutting, who knows how long it will take to actually deliver the ATF. In the meantime, the F-15E is a good concept based on a rugged and proven airframe. Even if the ATF does come along, an aircraft capable of delivering 24,000 pounds of ordnance deep behind enemy lines day or night, good weather or bad, and capable of air-to-air combat with the best the enemy has, will certainly find a role in any future conflict.</p>

