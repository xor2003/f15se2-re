<h3 class="h3"><b>Summary of F-15 Combat Action</b></h3>
<p class="p1">When President Gerald Ford accepted the first F-15 on behalf of the Tactical Air Command in November 1974, the aircraft was heralded as the best air-to-air fighter ever built. It's not surprising, then, that the leaders of the Israeli Air Force were interested in the F-15. A deal was struck, and in December 1976, the Israelis received their first shipment of F-15 fighters.</p>
<p class="p">With the tensions and hostilities in the Middle East during this time, it was probably inevitable that Israel would be the first to use the F-15 in combat.</p>
<h4 class="h4"><b>The Middle East</b></h4>
<p class="p1">In the spring of 1979, the Palestine Liberation Organization (PLO) drastically increased its terrorist attacks on Israel. In retaliation, Israel began a new wave of reconnaissance flights and bombing attacks on suspected terrorist camps and training facilities in Lebanon. Syria, in support of the PLO, began to fly air patrols over PLO camps in central Lebanon. The Syrian planes, mostly MiG-21s, had come close to Israeli reconnaissance planes on several occasions, but had never moved to within missile range or made any hostile moves.</p>
<a id="page_12"></a>
<h4 class="h4"><b>F-15 Versus MiG-21</b></h4>
<p class="p1">On 2 June 1979, Israeli F-4 Phantoms and A-4 Skyhawks flew to attack suspected terrorist bases near Sidon in southern Lebanon. Flying cover for the attack aircraft was a force of six F-15s and two Israeli-built Kafir fighters. All aircraft were guided by an E-2C AWACS (Airborne Warning And Control System) aircraft, a modified 747 that carried sophisticated radar and tracking equipment.</p>
<p class="p">At approximately 11:00 a.m., the attack force was bounced by 8 to 12 Syrian MiG-21 aircraft. The F-15s and Kafirs immediately identified and engaged the MiG-21s. Within a three-minute period, six Syrian MiG-21s were downed and several others were reportedly damaged.</p>
<p class="p">Of the six downed MiG-21s, four were destroyed by infrared homing missiles, one by a Sparrow radar-guided missile, and one by gunfire at close range. Five &ldquo;kills&rdquo; were given to F-15s and one to a Kafir.</p>
<p class="p">The Israelis continued their reconnaissance flights, and on 19 September 1979, the MiG-23 made its combat debut when a flight of these newer Soviet-made aircraft fired air-to-air missiles at an Israeli F-4 Phantom. The F-4 was able to avoid the missiles (probably AA-8 Aphid infrared homing types) and return safely to base.</p>
<p class="p">The Israeli intelligence service determined that a similar attack was planned five days later. So, on 24 September 1979, F-15s were again flying high cover when the controlling AWACS aircraft detected a large group of MiG-21s approaching. In this second brief air battle, four more MiG-21s were shot down. Again no F-15s were lost.</p>
<h4 class="h4"><b>F-15 Versus MiG-25</b></h4>
<p class="p1">These skirmishes continued through 1980. In March 1981, the F-15 and the newest Soviet-built machine&#8212;the MiG-25, which was capable of Mach 3&#8212;met for the first time. On 13 March 1981, a MiG-25 attacked an Israeli F-4 flying a reconnaissance mission. An F-15 Eagle was vectored to intercept and shot down the MiG-25 with a Sparrow missile. This was the first time a MiG-25 had been successfully intercepted and shot down.</p>
<a id="page_13"></a>
<h4 class="h4"><b>Preemptive Osirak Reactor Strike</b></h4>
<p class="p1">Shortly afterward on 7 June 1981, a group of F-15s flew cover for a flight of eight F-16s that successfully destroyed the Osirak nuclear reactor in Iraq. After the attack, the Israeli planes returned by flying over Jordan. Despite this overt violation of Jordanian airspace, no aircraft were sent to oppose them. Possibly the Jordanians were aware of the recent routing of the Syrian Air Force by F-15s and didn't wish to replay the incident.</p>
<h4 class="h4"><b>Southern Lebanon</b></h4>
<p class="p1">In the spring of 1982, tensions along the Israeli-Lebanese border again reached crisis level. Israel warned the PLO. Rocket attacks fell on Israeli villages.</p>
<p class="p">On 6 June 1982 operation &ldquo;Peace for Galilee&rdquo; began as a large Israeli armored task force moved across the border into southern Lebanon. The intent was to remove the PLO, other terrorist groups, and the Syrians, from Lebanon. Israeli air attacks faced strong resistance in the form of surface-to-air missiles and Syrian MiGs.</p>
<p class="p">Israel launched an immediate attack on the SAM locations and quickly destroyed 17 sites. The attacking force was met by more than 60 Syrian MiG-21 and MiG-23 fighters. F-15s and F-16s were flying cover for the strike force and engaged the MiGs in one of the largest air battles since World War II.</p>
<p class="p">Twenty-nine MiGs were destroyed without a single Israeli loss. The Soviets were so shocked by the Syrian losses that the Deputy Commander of the Soviet Air Force was sent to investigate.</p>
<p class="p">Many air battles were fought over the next several days until 11 June 1982, when a cease-fire went into effect. During the period from 6 June to 12 June, approximately 86 MiGs and five helicopters were destroyed by Israeli F-15s and F-16s without a single plane lost in air-to-air combat.</p>
<p class="p">From the time the Israeli Air Force began flying the F-15 until the cease-fire in June 1982, their F-15s scored 58 kills with no losses against Syrian MiGs.</p>

