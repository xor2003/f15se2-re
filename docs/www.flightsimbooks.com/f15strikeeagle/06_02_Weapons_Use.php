<h4 class="h4"><b>Firing a Short-Range Missile at a Target Behind You</b></h4>
<p class="p1"><i>Thanks to the game programmers, your F-15 missiles can track and attack targets behind you. Learn to use this special capability.</i></p>
<p class="p1"><b>Situation:</b> There's an enemy aircraft behind you, flying at roughly the same speed. You can't outmaneuver it. Your opponent isn't closer than a half mile or farther away than two miles.</p>
<p class="p1"><b>Maneuver:</b> Fire a short-range missile; then break away hard from the attacker.</p>
<a id="page_115"></a>
<p class="p"><img src="115_1.jpg"/></p>
