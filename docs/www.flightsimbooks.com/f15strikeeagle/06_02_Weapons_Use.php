<a id="page_116"></a>
<h4 class="h4">Air-to-Ground</h4>
<p class="p1">If air-to-air combat can be described as an art, then air-to-ground bombing is more a science. Get the proper angle and proper altitude, put the pipper (sight) on the target, and hit the bomb release. Bombing in <i>F-15 Strike Eagle</i> is much simpler than bombing with the real thing. Actual bombing runs are very precise with dive angle, airspeed, and altitude all predetermined by the type of ordnance you're dropping. If you're a little too fast, your load will land long; too slow and you'll come up short.</p>
<p class="p">With <i>F-15 Strike Eagle,</i> however, you can bomb at any speed or dive angle and at altitudes up to approximately 3000 feet. You can drop bombs with your wings at any angle, even while flying inverted (this is a real test of your inverted flying skills). As long as you put the pipper on the target triangle, you'll score a hit.</p>
<a id="page_117"></a>
<p class="p">There are two basic techniques you can attempt on a bombing run. Whichever you choose, start your run at 100-percent power. Rapid maneuvering to avoid surface-to-air missiles or enemy fighters can use precious energy. When you're delivering bombs at 2000 feet, you can't afford to let your energy level get low. A stall at low altitude can mean the end of your mission.</p>
<p class="p">During any bomb run, keep an eye out for enemy fighters. Should one appear while you're on your approach, immediately arm and fire a medium-range missile. This will keep the enemy fighter busy and let you complete your run.</p>
