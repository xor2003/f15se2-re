<h3 class="h3"><b>Weapons Use</b></h3>
<h4 class="h4"><b>Air-to-Air</b></h4>
<p class="p1">Your internal cannon is a formidable weapon. When properly used, it can reach out and hit the enemy at extreme ranges.</p>
<a id="page_112"></a>
<h4 class="h4"><b>Head-On Gun Attack</b></h4>
<p class="p1"><b>Situation:</b> You're approaching enemy aircraft head-on.</p>
<p class="p1"><b>Maneuver:</b></p>
<ul type="bull">
<li><p class="p1">Line up with the enemy straight ahead at the same altitude(1).</p></li>
<li><p class="p1">Start firing when the enemy is approximately 1&#189; radar screen divisions away. The target will fly to meet the gun shells. If you wait until the enemy aircraft is within normal gun range, you'll fly past before you get a good shot (2).</p></li>
</ul>
