<h4 class="h4"><b>Dive Bombing</b></h4>
<p class="p1">The standard bomb delivery technique is <i>dive bombing</i>. A dive bombing checklist should read like this:</p>
<ul type="bull">
<li><p class="p1">Check that you're at 100-percent power.</p></li>
<li><p class="p1">Go to 4000 feet (1).</p></li>
<li><p class="p1">Line up on the NAV indicator on the screen (assuming you've already positioned the NAV cursor over the target). Place the NAV indicator as close as possible to the center of your screen to reduce last-minute maneuvering.</p></li>
<li><p class="p1">When the blue target-designator triangle appears, arm your bombs.</p></li>
<li><p class="p1">Make any last-minute course adjustments and begin a 30-degree dive. The horizon should be level and on the third indicator line (2).</p></li>
<li><p class="p1">When the bomb sight is well within the target area, release the bombs (3).</p></li>
<li><p class="p1">Pull out of your dive (4).</p></li>
</ul>
<a id="page_119"></a>
<p class="p"><img src="119_1.jpg"/></p>
<em><b>Pinpoint dive bombing is a skill you'll need to practice.</b></em>
<a id="page_120"></a>
<h4 class="h4">Pop-Up Bombing</h4>
<p class="p1">The second method for delivering bombs, frequently used in high-threat situations, is the low-level or <i>pop-up</i> approach. If there are no enemy fighters around, approaching the target at approximately 1000 feet may give you some protection from radar-guided SAMs. The missiles should pass right over you. Flying at this altitude takes constant attention, though, since turbulence will constantly buffet your aircraft. A pop-up bombing checklist should read like this:</p>
<ul type="bull">
<li><p class="p1">Check that you're at 100-percent power.</p></li>
<li><p class="p1">Place the NAV cursor over your target. Line up on the NAV indicator on the screen.</p></li>
<li><p class="p1">Set your radar to medium-range scale.</p></li>
<li><p class="p1">Descend to 1000 feet and arm your bombs (1).</p></li>
<li><p class="p1">When the target appears on radar, immediately climb to 2000 feet (2).</p></li>
<li><p class="p1">When you reach 2000 feet, or the blue target indicator appears on the head-up display, start a 30- or 40-degree dive (whatever it takes to place the pipper in the triangle) and release the bombs (3).</p></li>
<li><p class="p1">At this point, you can return to the 1000-feet level to attack another target, or light the afterburners and head for home or the safety of high altitude (4).</p></li>
</ul>
<a id="page_121"></a>
<p class="p"><img src="121_1.jpg"/></p>
<em><b>Use this pop-up bombing technique when you want to fly in under the ememy rodar.</b></em>
<a id="page_122"></a>
<h4 class="h4"><b>High-Altitude Dive Bombing</b></h4>
<p class="p1">If you're coming up on a target you want to bomb at high altitude, you don't have to pass it by. Such a target can be hit by following these guidelines:</p>
<ul type="bull">
<li><p class="p1">Cut power to 55 percent.</p></li>
<li><p class="p1">Line up the target directly in front of you.</p></li>
<li><p class="p1">Set the radar to the shortest range.</p></li>
<li><p class="p1">When the target appears on the radar, activate the bomb sight and start a steep dive.</p></li>
<li><p class="p1">Keep track of your progress by watching the radar screen. Adjust your dive if necessary to prevent passing the target.</p></li>
<li><p class="p1">The target triangle should appear on the ground as you pass 10,000 feet. If it doesn't appear, you've probably passed over the target.</p></li>
<li><p class="p1">Drop the bombs and/or pull up at 3500 feet. If at any time during your dive you lose control, extend the speedbrake to stabilize the aircraft.</p></li>
</ul>

