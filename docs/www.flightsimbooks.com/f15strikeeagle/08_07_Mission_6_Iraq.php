<h4 class="h4">Mission 6: Iraq, 1981</h4>
<p class="p"><b>Scenario:</b> Etzion Air Base in the Sinai Desert is normally a very busy place. On 7 June 1981, it was even busier than usual. Early that morning, a group of Israeli F-15s and F-16s began a highly controversial mission. The Iraqi nuclear reactor south of Baghdad was about to become operational and the Israelis believed it was capable of producing weapon-grade fissionable material. In anticipation of its completion, the Israeli Air Force had been practicing an attack on the reactor for over a year. The time had come for the plan to go into action.</p>
<p class="p">The attack force flew around Jordan, through Saudi Arabia, and into Iraq low and fast. When questioned by Jordanian<a id="page_145"></a> air controllers, they convinced them that they were Saudis on a training flight. The attack was carried out so quickly that neither SAMs nor Iraqi aircraft had time to react. The one-ton bombs carried by the F-16s were extremely accurate. The reactor and surrounding buildings were destroyed.</p>
<p class="p"><b>Tactics:</b> When flying the simulation, you won't have the benefit of reality&#8212;you'll experience heavy SAM and occasional MiG opposition.</p>
<p class="p">This mission can be completed in one flight with a little luck and a lot of jinking to avoid SAMs. Again, fuel conservation is very important, so immediately drop all but two bomb loads and shoot off half your missiles.</p>
<p class="p">Descend to below 1500 feet and head straight toward the target. You'll receive a lot of attention from the SAM operators, so be prepared to respond with jinks and brief afterburner spurts to avoid the infrared-homing missiles.</p>
<p class="p">Make your bomb run; then perform a vertical half-loop and hit the afterburners. Set the NAV cursor for home base and climb to 35,000 feet to avoid the infrared-homing SAMs. Continue on afterburners until you're out of range of SAMs; then keep your speed up over 1500 knots by losing altitude as you approach the base.</p>
<p class="p">Be sure to lose altitude fast enough because fuel will be critical at this point. Also be alert for enemy aircraft. There won't be many, but they can show up at the worst time.</p>

