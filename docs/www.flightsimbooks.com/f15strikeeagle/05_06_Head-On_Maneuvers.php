<h4 class="h4"><b>Head-On Turning Attack</b></h4>
<p class="p1"><b>Situation:</b> You're approaching an opponent head-on at slow speed (400-500 knots). This speed allows maximum turning performance, which is to your advantage. Remember to increase the throttle during the turn to prevent a stall.</p>
<p class="p1"><b>Maneuver:</b></p>
<ul type="bull">
<li><p class="p1">Head slightly to one side of your opponent to get some lateral separation during the initial pass (1).</p></li>
<li><p class="p1">Turn hard toward your opponent as you pass (2).</p></li>
<li><p class="p1">Maintain this maximum-G turn. You may need to increase power during the turn to keep your speed up to 400 knots (3).</p></li>
<li><p class="p1">If you can out-turn your opponent, you should get a missile shot (4).</p></li>
</ul>
<p class="p">This isn't a long-term maneuver. If you don't gain an advantage fairly quickly, be ready to disengage or attempt another maneuver, such as a Low Yo-Yo. Spinning around and around with a single bogey will likely draw a number of his buddies to the area. Don't get so wrapped up that you let another bogey come in and gun you down.</p>
<a id="page_107"></a>
<p class="p"><img src="107_1.jpg"/></p>
<p class="p">Your turning performance must be better than your opponent's for this maneuver to work.</p>
<a id="page_108"></a>
<h4 class="h4"><b>A Series of Low Yo-Yos</b></h4>
<p class="p1">After a head-on pass, you can use a series of Low Yo-Yo maneuvers (described and illustrated earlier) to pull around on the tail of the bogey. This maneuver can be used even if you don't have a turning advantage over your opponent.</p>
<p class="p">Yo-Yo maneuvers are some of the best moves you can learn. Master them, and they'll come in handy in a number of situations.</p>

