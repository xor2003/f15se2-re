<h3 class="h3"><b>Head-On Maneuvers</b></h3>
<p class="p1">Many dogfights start from a neutral head-on pass after both parties have survived an initial missile exchange. Remember, it's mission first. You always have the option of not turning and fighting after a head-on pass. If you're close to your ground target, you may want to consider this option, but if you do choose to go toe-to-toe with the bogey, here are a few moves that might help you gain the advantage.</p>
<p class="p1"><b>Pitch Back</b></p>
<p class="p1"><b>Situation:</b> You're attacking your opponent head-on with at least 500 knots of airspeed.</p>
<p class="p1"><b>Maneuver:</b></p>
<ul type="bull">
<li><p class="p1">You're in a head-on pass (1).</p></li>
<li><p class="p1">Pull up into a loop maneuver (2).</p></li>
<li><p class="p1">At the top of the loop, keep the stick pulled back (3).</p></li>
<li><p class="p1">When you're heading straight toward the ground, start to turn toward the enemy while pulling out of the dive (4).</p></li>
<li><p class="p1">Instead of finishing the loop heading in your original direction, you've turned on the way down and should finish in a hard turn behind your opponent (5).</p></li>
</ul>
<a id="page_103"></a>
<p class="p">This kind of maneuver is called an <i>out-of-plane</i>&#8212;you're maneuvering in the vertical plane while your opponent is in a flat turn. This is a very good opening move if you suspect you can't out-turn your opponent due to greater airspeed. Use your energy advantage to go vertical.</p>
<p class="p"><img src="103_1.jpg"/></p>
<p class="p">This maneuver looks tricky&#8212;and it is. Pull back hard on the stick, loop, and then turn behind the target if it's there.</p>
<a id="page_104"></a>
<h4 class="h4"><b>Head-On Gun Attack</b></h4>
<p class="p1"><b>Situation:</b> You're approaching enemy aircraft head-on. You're too close for a missile shot, but you can still take a guns shot before he flashes past you.</p>
<p class="p1"><b>Maneuver:</b></p>
<ul type="bull">
<li><p class="p1">Line up with the enemy straight ahead, at the same altitude (1).</p></li>
<li><p class="p1">Start firing when the enemy is approximately 1&#189; radar screen divisions away. The target will fly to meet the gun shells. If you wait until the enemy aircraft is within normal gun range, you'll fly past before you get a good shot (2).</p></li>
</ul>
