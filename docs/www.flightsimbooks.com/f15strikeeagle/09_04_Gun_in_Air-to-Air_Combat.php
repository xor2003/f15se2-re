<h3 class="h3"><b>Gun in Air-to-Air Combat</b></h3>
<p class="p1">In a tight turning &ldquo;knife fight in a phone booth,&rdquo; your 20mm cannon will be indispensable. The manual does a good job of explaining the historical gunsight on page 47. Take a minute to review this section if it isn't familiar to you.</p>
<p class="p">The main thing to master is <i>anticipation firing</i>. Use the track cam and your view of the bogey to get a good idea of the direction he's heading; then maneuver toward him and watch your gunsight start to move. After that, it's just a matter of watching both the gunsight and the bogey and maneuvering so that their paths cross. When the gunsight starts to move toward the target, you need to anticipate and fire just before they cross so your shells and the bogey will both arrive at the same point at the same instant.</p>
<p class="p">Also be aware that due to the system programming, it can handle only one input at a time. If you're moving the joystick to maneuver the plane, that will normally override the command to shoot if you squeeze the trigger. To overcome this, you need to ease off the joystick when you're about to shoot so the fire command will be the only input at that second.</p>

