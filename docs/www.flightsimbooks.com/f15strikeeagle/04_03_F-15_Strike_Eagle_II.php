<h3 class="h3"><b><i>F-15 Strike Eagle II</i></b></h3>
<h4 class="h4"><b>Simulation Setup for Air Training and Practice</b></h4>
<p class="p1"><b>Skill level:</b> Rookie</p>
<p class="p1"><b>Theater:</b> Libya</p>
<p class="ul">&bull; Start the simulation.</p>
<p class="ul">&bull; Hold the Alt key down while pressing the T key to put the simulation in Training mode. Now enemy fire won't damage your aircraft.</p>
<p class="ul">&bull; Arm and fire a short range missile to dispatch with the MiG that appears at the start of each mission.</p>
<p class="ul">&bull; Turn and take a heading of 000, away from the land mass. If there's cloud cover on that heading, you can veer away from it or climb so that you're above the clouds. You'll need a good view of the horizon to practice your maneuvers. While holding down the Alt key, tap the S key several times to <i>Slew</i>, or transport, your aircraft to the north where you'll be able to practice in peace. This function only works in the Training mode.</p>
<p class="ul">&bull; To improve the plane's performance, you can fire all air-to-ground weapons before practicing, but you should retain your air-to-air missiles to deal with any MiGs that show up to interrupt your valuable practice time.</p>
<p class="p">Now you're ready to practice maneuvers. Feel free to use all the time you need. You may be approached by enemy aircraft after awhile. If one (or more) appears on radar, turn to face it and fire medium-range missiles at it.</p>
