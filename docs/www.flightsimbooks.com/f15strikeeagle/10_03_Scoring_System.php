<h3 class="h3"><b>Scoring System</b></h3>
<p class="p1">The system MicroProse uses in most of its games for tallying your score includes a base level score for each target hit. That score is then modified based on a number of considerations. Such is the case with <i>F-15 Strike Eagle II.</i></p>
<h4 class="h4"><b>Base Scores</b></h4>
<p class="p1"><b>Enemy aircraft:</b> 50</p>
<p class="p1"><b>Enemy ground targets (not primary or secondary):</b> 25</p>
<p class="p1"><b>Primary mission target:</b> 200</p>
<p class="p1"><b>Secondary mission target:</b> 100</p>
<p class="p1"><b>Friendly aircraft:</b> -100</p>
<p class="p1"><b>Friendly ground installations:</b> -50</p>
<a id="page_171"></a>
<p class="p">These scores are then multiplied by a number based on the skill level or the region selected.</p>
<h4 class="h4"><b>Aircraft Scores</b></h4>
<p class="p1">The number of points awarded for enemy aircraft shot down is based on the skill level you choose. The number of points awarded doesn't change with the various regions. These totals are:</p>
<p class="p1"><b>Rookie:</b> 50 points</p>
<p class="p1"><b>Pilot:</b> 100 points</p>
<p class="p1"><b>Veteran:</b> 150 points</p>
<p class="p1"><b>Ace:</b> 200 points</p>
<p class="p">An enemy aircraft shot down at Ace level over Libya will give the same points as one shot down over the Middle East. The point penalty for shooting down a friendly plane is usually double that of the score awarded for destroying an enemy plane, so it's very important to use caution when operating close to friendly airfields.</p>
<h4 class="h4"><b>Ground Target Scores</b></h4>
<p class="p1">Points awarded for ground targets vary with the region in which you've selected to fly, <i>and</i> they change according to skill level. Since all primary and secondary targets are ground targets, this rule applies to them as well.</p>
<p class="p">Regions offer more points as you move through them in the order they're presented. Libya is the easiest, followed by the Persian Gulf, then Vietnam, and then the Middle East, which is the most difficult.</p>
<p class="p">The base score, as listed above, is multiplied by a number, depending upon which region you're in and the skill level selected. These are a little more complicated to calculate. They range as follows:</p>
<p class="p1"><b>Primary targets:</b> 200-3000</p>
<p class="p1"><b>Secondary targets:</b> 100-1500</p>
<p class="p1"><b>Other ground targets:</b> 25-375</p>
<p class="p">Different combinations of regions and skill levels will give you different scores in these ranges. Penalties for hitting friendly targets go up as well so, once again, caution is advised.</p>
<a id="page_172"></a>
<h4 class="h4"><b>Mission End</b></h4>
<p class="p1">How you end the mission will affect your score as well. Once your overall total has been calculated, it may change if you don't have a proper landing. If you crash, your score is reduced by half. A successful bailout will give you three-fourths your total score.</p>
<p class="p">Your total score may also be reduced if it takes you more than one trip to destroy both the primary and secondary targets. If two trips are required, you'll get two-thirds your total; if three trips are required, you'll get one-half your score, and so on. It's very important to destroy both targets in one trip.</p>

