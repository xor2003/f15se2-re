<h4 class="h4">Mission 3: Haiphong, North Vietnam, 1972</h4>
<p class="p"><b>Scenario:</b> Following a North Vietnamese incursion across the Demilitarized Zone (DMZ) in late March, bombing of North Vietnamese targets was again authorized.</p>
<p class="p">This mission is based on the missions flown by F-4 and A-6 pilots during the bombing campaign code named <i>Linebacker I</i>.</p>
<p class="p"><b>Tactics:</b> Take advantage of the poor, low-level performance of enemy radar used to guide the SA-2 and SA-3 SAMs.</p>
<p class="p">Immediately after starting the simulation, descend to below 1500 feet and stay there. Enemy MiGs weren't active during the first stages of Linebacker I and shouldn't be a problem.</p>
<p class="p">Place the NAV cursor over the first target and make your bomb run. If you don't have another person helping as weapons officer, it's best to pass over the second target and then regroup.</p>
<p class="p">Place the NAV cursor and turn around making your second bomb run so you'll be heading out to sea once it's completed.</p>

