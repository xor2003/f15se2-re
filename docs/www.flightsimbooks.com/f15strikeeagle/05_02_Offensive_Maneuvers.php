<tr>
<td align="center"><p><b>CAUTION</b></p></td>
</tr>
<tr>
<td align="left"><p>While climbing, you may present your opponent with a good missile shot. Take an occasional look out the back and try to time your zoom so that the bogey isn't in a position to shoot.</p></td>
</tr>
</table>
<a id="page_89"></a>
<p class="p"><img src="089_1.jpg"/></p>
<em><b>Your jet can perform a ballistic climb. Use this advantage to put some distance between you and the enemy.</b></em>
<a id="page_90"></a>
<h4 class="h4"><b>Dive for Separation for a Missile Shot</b></h4>
<p class="p1"><b>Situation:</b> You're in a very close, hard-turning fight and you've climbed to high altitude in the process. You're too close for a missile shot, but you can't line up your hard-maneuvering opponent for a gun shot. This maneuver puts distance between the two of you for a better chance at a missile hit.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Bank hard and try to pull inside the enemy's turn (1).</p>
<p class="ul">&bull; Roll inverted, pull back on the stick and dive (2).</p>
<p class="ul">&bull; Keep the nose pointed straight down until you've lost 10,000 feet or so. Then pull back hard on the stick and keep it pulled back until you pull out of the dive and head back up toward the target.</p>
<p class="ul">&bull; Locate the enemy aircraft and fire a short-range missile (3).</p>
