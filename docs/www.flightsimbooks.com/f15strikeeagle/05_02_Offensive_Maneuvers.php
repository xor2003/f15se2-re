<h3 class="h3"><b>Offensive Maneuvers</b></h3>
<p class="p1">As Boelcke said, once you start an attack, carry it through to the end. These maneuvers will help you do just that. If you start the fight with an advantage, use one of the following to set up the bogey for the kill. Remember to watch your speed, and don't overshoot.</p>
<a id="page_80"></a>
<h4 class="h4"><b>Low Yo-Yo</b></h4>
<p class="p1"><b>Situation:</b> You're in a hard-turning, low-speed fight. You're too close for short-range missiles but you can't out-turn the enemy to line up for a gun shot.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Let your upper wing come over, and begin an inverted dive (1). The speed you gain in the dive will let you hold a tight turn toward the bogey without stalling.</p>
<p class="ul">&bull; Roll out in the opposite direction and pull the nose up (2).</p>
<p class="ul">&bull; Repeat the process until you pull in behind the enemy and can line up a gun shot (3). Be quick on the trigger and ready to fire as you pull up each time. Firing parameters may be met for only a second or two.</p>
<table class="t2" border="1" rules="none" cellpadding="5" cellspacing="0" width="50%" align="center">
<tr>
<td align="center"><p><b>NOTE</b></p></td>
</tr>
<tr>
<td align="left"><p>This maneuver should not be performed at low altitude.</p></td>
</tr>
</table>
<a id="page_81"></a>
<p class="p"><img src="081_1.jpg"/></p>
<em><b>Drop down inside the other aircraft's turn; then pull in behind for a possible shot.</b></em>
<a id="page_82"></a>
<h4 class="h4"><b>High Yo-Yo</b></h4>
<p class="p1"><b>Situation:</b> You're closing rapidly on a turning target from the side or rear. You want to:</p>
<p class="ul">&bull; Avoid overshooting the target and losing your offensive position due to your greater speed.</p>
<p class="ul">&bull; Obtain a better position behind the target for improved heat-seeking missile tracking.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Level your wings and pull up to gain altitude (1).</p>
<p class="ul">&bull; Begin turning toward the target, remaining above and behind (2).</p>
<p class="ul">&bull; At this point, start an inverted roll and dive at the target (3). You have two options here: You can point your nose slightly ahead of the target and take a Lead-pursuit path to make a diving guns pass, or you can come around behind the target for a heat-seeking missile shot directly at the target's tailpipe (4).</p>
<p class="ul">&bull; If the target reverses its turn at position 5, you should get a clean shot at its tail. If you fail to destroy the opponent, or if you miss, another High Yo-Yo or a Low Yo-Yo will return you to an attacking position.</p>
<table class="t2" border="1" rules="none" cellpadding="5" cellspacing="0" width="50%" align="center">
<tr>
<td align="center"><p><b>WARNING</b></p></td>
</tr>
<tr>
<td align="left"><p>Be careful not to bleed off too much speed during the climbing portion of this maneuver or the bogey may be able to accelerate out of range.</p></td>
</tr>
</table>
