<tr>
<td align="center"><p><b>CAUTION</b></p></td>
</tr>
<tr>
<td align="left"><p>Diving with afterburners on will cause a rapid loss of altitude and may cause you to exceed the maximum design speed for the aircraft.</p></td>
</tr>
</table><a id="page_91"></a>
