<p class="p"><img src="083_1.jpg"/></p>
<em><b>While the other aircraft is breaking hard, pull up; then pull down to get behind him.</b></em>
<a id="page_84"></a>
<h4 class="h4"><b>Straight Yo-Yo</b></h4>
<p class="p1"><b>Situation:</b> The enemy aircraft is making a break for it, trying to outrun you. In this situation, if you don't want to use your afterburners because your fuel level is low, you can trade altitude for speed.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Obtain a position directly behind the enemy aircraft (1).</p>
<p class="ul">&bull; Remaining directly behind your opponent, begin a shallow (10- to 20-degree) dive (2).</p>
<p class="ul">&bull; Notice your airspeed rising. As you close on the enemy, pull up to slow down and prevent an overshoot (3).</p>
<a id="page_85"></a>
<p class="p"><img src="085_1.jpg"/></p>
<em><b>Trade altitude for speed by going into a shallow dive and then pulling up when you're within range.</b></em>
<a id="page_86"></a>
<h4 class="h4"><b>Barrel Roll Attack</b></h4>
<p class="p1"><b>Situation:</b> You're approaching an opponent from behind. The other aircraft turns hard, and your speed advantage may cause an overshoot.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; The enemy aircraft breaks hard to the right. You should pull up hard (1).</p>
<p class="ul">&bull; Immediately begin a Barrel Roll to the left, away from your opponent (2). This should cause you to bleed off some speed so you don't overshoot.</p>
<p class="ul">&bull; Finish the maneuver by sliding back in behind the enemy plane with a hard right turn (3).</p>
<a id="page_87"></a>
<p class="p"><img src="087_1.jpg"/></p>
<em><b>Instead of overshooting, pull up and roll away from your opponent. Slide back into firing position with another turn&#8212;this time toward the target.</b></em>
<a id="page_88"></a>
<h4 class="h4"><b>Zoom Maneuver from a Turn</b></h4>
<p class="p1"><b>Situation:</b> You're in a hard-turning fight. You can't obtain an advantage, and you're too close for a missile shot. Both you and your opponent are at low speed. The object here is to use your high thrust-to-weight ratio to perform a steep climb so you can get enough distance between you and the enemy to use your missiles.</p>
<p class="p1"><b>Maneuver:</b></p>
<p class="ul">&bull; Fire your afterburners (1).</p>
<p class="ul">&bull; Execute a very steep climb (2).</p>
<p class="ul">&bull; Continue until you've climbed 5000 feet or so. Throttle back your engines and perform a stall turn (3).</p>
<p class="ul">&bull; As soon as your opponent comes into view, fire a short-range missile.</p>
