<h4 class="h4">Mission 4: Syria, 1984</h4>
<p class="p"><b>Scenario:</b> When the Syrians started moving modern Soviet-built SA-9 SAMs near the border, the Israelis decided to act. The locations of the emplacements were determined using RPVs (Remotely Piloted Vehicles). These small unpiloted drones carried cameras and accurately located the targets. F-15s and F-16s then flew a number of successful bombing missions. The Syrians used heavy smoke in an attempt to hide the locations of the SAMs, but the Israeli preflight homework paid off.</p>
<p class="p"><b>Tactics:</b> This is the first mission with all the threats working against you. The Syrians have radar and infrared-homing SAMs, MiG-21s, and MiG-23s. Your primary target is a long way from home; the distance can be a very real threat in itself.</p>
<p class="p">This mission is best handled in two phases. First, take out the two closest SAM and airport locations. This will make it easier to get in and out during the second phase. After taking out the first four targets in Phase 1, return to base to rearm, refuel, and repair.</p>
<a id="page_142"></a>
<p class="p">You can fly more missions to soften up the path to the primary target, but the mission can be completed on the second flight by flying in fast toward the target with a light load of two or three bomb loads. If you're really confident, take only one. (The fewer the better, because fuel conservation is important.)</p>
<p class="p">Afterburners can be used for a while, but not for the whole trip. Go straight in, make your bomb run, and head straight home. Heading home can be done at high altitude if you've cleared out most of the radar-guided SAMs. If you do run out of fuel, don't bail out immediately since the plane will glide a considerable distance with no bomb load.</p>
