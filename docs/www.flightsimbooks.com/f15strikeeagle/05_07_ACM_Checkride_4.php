<h3 class="h3"><b>ACM Checkride &#35;4</b></h3>
<p class="p1"><i>Head-to-head maneuvering can be difficult to master. This ride gives you a chance to practice your moves.</i></p>
<p class="p1"><b>Simulation setup:</b> The setup and objectives are the same for both versions of <i>F-15 Strike Eagle</i> except where otherwise noted.</p>
<p class="p1"><b>Level:</b> Pilot</p>
<p class="p1"><b>Mission:</b> Libya</p>
<p class="p1"><b>Objective:</b> To start out in a neutral head-on fight and gain the offensive and destroy the bogey.</p>
<p class="p1"><b>Procedure:</b> Start the simulation and take off. Light your afterburners and turn and accelerate away from the MiG that's in the area. Once you're several miles away, reduce your throttle, make a wide turn, and face the MiG in a head-on pass. Jink or decoy any missiles fired by the bogey, but hold your missile fire until after the first pass.</p>
<p class="p">During your missions, you'll want to avoid this type of fight if at all possible. History shows that most successful air victories have come from ambush. Attack from long range whenever possible, and only get into tight turning battles as a last resort and on your terms.</p>

