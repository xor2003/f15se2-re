<h3 class="h3"><b>ACM Checkride &#35;2</b></h3>
<p class="p1"><i>The purpose of this checkride is to let you demonstrate that you've learned to finish off an opponent when you have the advantage.</i></p>
<p class="p1"><b>Simulation setup:</b> Setup and objectives are the same for both versions of <i>F-15 Strike Eagle</i> except where noted.</p>
<p class="p1"><b>Level:</b> Pilot</p>
<p class="p1"><b>Mission:</b> Libya</p>
<p class="p1"><b>Objective:</b> To achieve success using all the maneuvers just discussed.</p>
<p class="p1"><b>Procedure:</b> Start the simulation and take off. A MiG should appear close by, but out in front of you. Let him go for a second or two; then use one of the offensive maneuvers to get back on his tail and shoot him down with either a short-range missile or guns. After a successful attack, restart the simulation and try a different tactic.</p>
<p class="p">When you find a maneuver that works well for you, practice it until it becomes second nature. Every great fighter has one good knock-out punch.</p>

