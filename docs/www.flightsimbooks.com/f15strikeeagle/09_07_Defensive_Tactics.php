<h3 class="h3"><b>Defensive Tactics</b></h3>
<p class="p1">Turning and dogfighting with enemy aircraft has already been covered fairly well. Do what you must to survive. The other major threat, probably more dangerous than enemy aircraft, is <i>surface-to-air missile</i> (SAM) fire. SAMs will be coming at you almost continuously during your time behind enemy lines. Learning how to effectively deal with them will spell the difference between success and failure.</p>
<a id="page_163"></a>
<h4 class="h4"><b>Know the Missile Type</b></h4>
<p class="p1">When the tone sounds indicating a missile launch, it's imperative that you look up to the top of the HUD for information on the type of missile. The capabilities of the various enemy missile systems vary greatly. Some are extremely dangerous and some are easily fooled. Knowing which kind is coming at you will determine your response.</p>
<p class="p">You'll only have a second or two to look up and see the missile type displayed. Once it's gone, you won't know for sure what kind of missile is headed your way. You can, however, get some idea of the missile's capabilities by dropping a bundle of chaff while it's still a long way off. If the tracking indicator light goes off when you drop the chaff, you'll know the missile isn't Doppler guided and can be fooled easily. If it doesn't go out, you'll know the missile has a sophisticated Doppler guidance system that chaff alone won't fool.</p>
<h4 class="h4"><b>Avoiding Radar Guided SAMs and AAMs</b></h4>
<p class="p1">Radar guided SAMs and air-to-air missiles are the enemy's most lethal weapons. Infrared (or heat) guided missiles can be trouble but are generally easy to fool by a flare.</p>
<p class="p">Radar-guided missiles fall into two categories: <i>beam-riders</i> and <i>semiactive</i> Doppler systems.</p>
<p class="p"><b>Beam-rider missiles.</b> These are generally older and are easily fooled. They follow the radar beam from a ground or air source to the target. Since the source is a long way off, it's easier to decoy or jam it. Chaff will cause beam-rider missiles to lose &#34;lock-on.&#34; Once the lock is lost, they continue flying in a straight course. Therefore, after you dispense a bundle of chaff, you must also change course if the missile is coming straight at you from the rear or from the front. This explains why occasionally you may decoy a missile, but it hits you anyway.</p>
<p class="p">Detection by beam-rider type systems can be delayed by flying low and directly toward the source of the radar signal. This reduces the system's effectiveness.</p>
<p class="p"><b>Semiactive Doppler missiles.</b> These are much harder to shake. They use a Doppler detection system that's extremely good at detecting targets moving toward or away from the source. They also have a small radar transmitter built into the<a id="page_164"></a> nose of the missile. As the missile gets close to you, it can burn through jamming and see through chaff. Chaff alone won't fool them.</p>
<p class="p">To defeat this type of system, you must use the strength of the Doppler system against it. Since it's very good at detecting targets moving either toward or away from it, you must turn so the missile is directly off to one side. You need to maneuver so that your path is at a right angle to the path of the missile. This will keep you from moving toward or away from it as much as possible. Once you get turned to the correct perpendicular course, the radar tracking light will go out and the missile on the radar screen will turn dark, indicating that the missile has lost its radar lock. If you continue on this course, the missile will eventually pass harmlessly behind you.</p>
<p class="p">As you increase the difficulty levels, this safe area gets smaller and smaller. At Veteran and Ace levels, you may have to constantly maneuver to keep perfectly perpendicular to the missile to keep it from locking on to you. When flying at the higher levels of difficulty, it's a good idea to use the shortest range on the radar as the missile approaches you. It isn't uncommon for you to move out of the safe zone just as the missile is passing behind you, which may give the missile a chance to acquire you at the last second and score a hit. If you're watching on the radar screen, you'll see the missile start to flash again; you should then be able to maneuver back into the safe area.</p>
<p class="p">There are only three SAM missiles that have this system. The ones you should worry about are:</p>
<p class="p1"><b>SA-10</b></p>
<p class="p1"><b>SA-12</b></p>
<p class="p1"><b>SA-N-6</b> (fired from missile boats)</p>
<a id="page_165"></a>
<p class="p">There's one air-to-air missile of this variety: the AA-10, and it can be very tough. Front-line fighters may be found with them, and they almost always show up on enemy Il-76 Mainstay AWACs aircraft.</p>
