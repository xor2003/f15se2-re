<h4 class="h4"><b>Optimize the Use of Your Chaff</b></h4>
<p class="p1">You don't have an unlimited amount of chaff, so use it wisely. Once you get over enemy territory, it's likely that you may have missiles coming at you from a number of sources, several at a time. With proper timing and clever maneuvering, it's frequently possible to fool two or more missiles with a single bundle of chaff. This normally involves waiting until both missiles are close to you before deploying the bundle. Having an extra bundle or two when trying to limp home damaged may save you and your aircraft.</p>
<h4 class="h4"><b>Attacking High-Threat Targets</b></h4>
<p class="p1">Anytime you're trying to destroy a SAM radar, missile, or anything located next to a SAM site, you're heading into a <i>high-threat</i> area. If the SAM has Doppler-guided missiles, the threat is higher still. In order to get close enough to launch a Maverick, you'll have to head toward the target; however, heading <i>directly</i> toward it may not be the best path. An indirect route known as an <i>offset approach</i> might be a safer choice.</p>
<a id="page_166"></a>
<p class="p"><img src="166_1.jpg"/></p>
<em><b>Figure 9-4. Offset Attack on a High-Threat Target</b></em>
<a id="page_167"></a>
<p class="p1"><b><i>Position 1.</i></b> <i>To use this approach you start off heading directly toward the SAM site.</i></p>
<p class="p1"><b><i>Position 2.</i></b> <i>When the site launches a missile at you, wait a second or two so you can get closer to the target; then turn away on a perpendicular course to fool the missile.</i></p>
<p class="p1"><b><i>Position 3.</i></b> <i>Once the missile is past, don't head directly back toward the target; instead, head off to the side.</i></p>
<p class="p1"><b><i>Position 4.</i></b> <i>When the site launches another missile at you, you can now turn back</i> toward <i>the site to get perpendicular and fool the missile.</i></p>
<p class="p1"><b><i>Position 5.</i></b> <i>Now when clear again, you may be close enough to head directly into the target and attack it before it can get another shot off.</i></p>

