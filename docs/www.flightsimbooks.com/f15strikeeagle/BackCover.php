<p class="p1">Day or night, through perilous weather, deep in enemy-controlled territory&#8212;when national security is threatened, you, <i>F-15 Strike Eagle</i> pilot, are there on the front line of action.</p>
<p class="p">From takeoff to landing. <i>The Official F-15 Strike Eagle Handbook</i> trains you to plan and complete your mission with the skill and flair of as real ace. A roundtable of F-15 experts, including Bill Stealey, President of MicroProse Software, and Sid Meier, author of <i>F-15 Strike Eagle</i>, share tips and information for completing your mission with the most points and a minimum of bailouts.</p>
<p class="p">Here you'll find:</p>
<ul>
<li><p class="p3">Specialized tactics, tips, and complete mission briefings</p></li>
<li><p class="p3">Illustrations of advanced flight maneuvers and real-life battle tactics</p></li>
<li><p class="p3">A complete run-down of the jet fighter's arsenal, plus how to choose the most effective weapons far your mission</p></li>
<li><p class="p3">Mission-saving information flagged with <i>caution</i> and <i>warning</i> boxes</p></li>
<li><p class="p3">Points, decorations, and the promotion system demystified</p></li>
<li><p class="p3">Military abbreviations, acronyms, and flight jargon completely explained</p></li>
</ul>
<p class="p4">Richard Sheffield, a flight combat simulator veteran and author of numerous simulator books, has written the perfect companion to the <i>F-15 Strike Eagle II</i> manual. So charge up your afterburners, clear the map, and go for the glory. Whether you're perfecting the basics of flying or learning the advanced manuevers needed to fly victorious in the sky-blue theatre of superjets and smart missiles, <i>The Official F-15 Strike Eagle Handbook</i> carries you from takeoff to mission's end.</p>
<p class="p4">Happy hunting!</p>
