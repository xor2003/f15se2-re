<h4 class="h4">Limping Home</h4>
<p class="p1">A damaged aircraft is often a fact of life in <i>F-15</i>. Nursing a wounded bird back to the nest can take some doing. If you decide you're going to make a dash back to the base for repairs, the first thing to do is drop any remaining bomb loads and the external fuel tank. Set your navigational cursor. Either hit the burners for a fast escape, or slow down, get low, and try to sneak back.</p>
<p class="p">The aircraft tends to handle best at low speeds (around 240 knots) when damaged, but if you're over enemy territory, you'll be an easy target at that speed. Remember, the aircraft is much harder to handle at high speed when damaged.</p>

