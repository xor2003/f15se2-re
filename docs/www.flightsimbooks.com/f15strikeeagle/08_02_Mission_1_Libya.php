<h4 class="h4">Mission 1: Libya, 1981</h4>
<p class="p"><b>Scenario:</b> On the morning of 19 August 1981, two F-14 Tomcats of U.S. Navy Squadron VF-41 (Black Aces) were patrolling 60 miles south of the carrier <i>Nitmitz</i> in the Gulf of Sidra near Libya.</p>
<p class="p">Two Libyan SU-22 Fitters were detected and the F-14s were dispatched to intercept. As the F-14s closed on the Libyans, one of the SU-22s fired a missile (probably an AA-2 Atoll). The F-14s broke away rapidly to avoid the missile and engaged the Fitters.</p>
<p class="p">In the short battle that followed, Squadron Leader Commander Henry &#34;Hank&#34; Kleeman; Lt. David Venlet, Kleeman's RIO (Radar Intercept Officer); Kleeman's wingman, pilot Lt. Lawrence &#34;Music&#34; Muczynski; and Muczynski's RIO, Lt. (j.g.) James &#34;Amos&#34; Anderson each shot down an SU-22 with an AIM-9L Sidewinder missile shot.</p>
<a id="page_140"></a>
<p class="p">Mission 1 is based on this incident and more. You get to do what the F-14 pilots couldn't: carry the attack to bases in Libya. Since this is the first mission, it's also the easiest. You won't encounter SAMs during the flight.</p>
<p class="p"><b>Tactics:</b> To complete this mission, quickly do away with the enemy plane you find in front of you as the simulation begins. A Sidewinder or several quick gun bursts should do the trick.</p>
<p class="p">Next, place the NAV cursor over the primary target, descend to 3000 feet, and fly toward the target. Use mediumrange missiles to keep other enemy aircraft busy while you make your bomb run.</p>
<p class="p">Drop as many bombs as possible on the target and pull up. With the main target destroyed, you can continue to attack ground targets, or drop the rest of your bomb load and head back to the coast where you can engage enemy planes until you run out of ammunition or fuel.</p>
<p class="p">When your fuel falls to 5000 pounds, it's time to head for home.</p>

