<h1 class="h1"><b>PART I. GROUND SCHOOL</b></h1>
<p class="p1">Before you earn your wings and step into an aircraft, you must spend hundreds of hours in intensive flight-training ground school. This is where all future pilots study&#8212;and in great detail&#8212;subjects ranging from aircraft mechanics to meteorology.</p>
<p class="p">The next few chapters will give you an overview of the topics covered in ground school as they pertain to operating in the Eagle Rapid Deployment Team. This is your opportunity to pick up knowledge you may have missed in the rush to qualify for flight training, or that you may have subsequently forgotten. The information is important; due diligence is expected on your part.</p>
<p class="p">Some of the topics covered are:</p>
<p class="ul">&bull; Development of the F-15 Strike Eagle</p>
<p class="ul">&bull; Pilot considerations</p>
<p class="ul">&bull; Basic aerodynamics</p>
<p class="ul">&bull; Weapons systems</p>
<p class="ul">&bull; Proper use of F-15 weapons</p>
<p class="ul">&bull; Theories of air combat</p>

