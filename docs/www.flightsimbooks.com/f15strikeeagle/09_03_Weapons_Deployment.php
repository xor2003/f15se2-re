<h3 class="h3"><b>Weapons Deployment</b></h3>
<h4 class="h4"><b>Cannon</b></h4>
<p class="p1">Many beginners often ignore the cannon in the air-to-ground role, but to get those really high scores, you need to pick up extra ground targets when you run out of Mavericks. The cannon lets you do this.</p>
<p class="p">The real key to learning to strafe ground targets is to get a set procedure and use it over and over again until it comes naturally. The following is a good example:</p>
<p class="ul">&bull; Line up the target early. Once you start to dive on the target, it's too late to make major course changes. Therefore, take the time to make sure you're perfectly lined up as you approach the target.</p>
<p class="ul">&bull; Approach the target at low altitude until the range to the target is 6km. This way, you can avoid detection until the last possible moment.</p>
<p class="ul">&bull; At 6km, pull up hard, being careful to maintain a straight course. Level out at 1000 feet or so. The exact altitude isn't important&#8212;you just want to make sure you have enough room to dive on the target. As your skill improves, you'll find that you require less altitude and can start your attack from lower.</p>
<p class="ul">&bull; Your gun is most effective when the range is less than 3km. Therefore, at 3km, extend your speed brake and start to dive on the target. Put the aiming circle either directly on the target or just a little in front of it. Fire a short burst and watch where the shells land. Make minor adjustments and continue to fire short bursts.</p></li>
<a id="page_156"></a>
<p class="ul">&bull; Pull up as soon as you hit the target or if you get below 400 feet. Unless you have the landing gear down or are diving very steeply, the aircraft will automatically pull up at 300 feet. As you gain experience, you'll be able to judge when to pull up by the size of the target, which will get larger as you approach the ground. <i>Don't wait too long before you pull up</i>. Most targets you would strafe are only going to give you a few points. Don't get so involved that you fly into the ground while trying to destroy a 30-point target.</p><a id="page_157"></a>
