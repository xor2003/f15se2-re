<h3 class="h3"><b>The Barrel Roll</b></h3>
<p class="p1"><b>Minimum Speed:</b> 400 knots</p>
<p class="p1"><b>Minimum Altitude:</b> 1000 feet</p>
<p class="p1">The main difference between a Barrel Roll and an Aileron Roll is that the nose of the aircraft stays level during an Aileron Roll. During a Barrel Roll the nose points up slightly during the first half of the maneuver, down during the second half, and then back level. This climbing and diving produces more drag; consequently, the Barrel Roll is used in many maneuvers to reduce the aircraft's speed.</p>
<p class="ul">&bull; Flying straight and level, pull back on the stick to achieve a 30-degree climb (third indicator line). As soon as a 30-degree climb is reached, apply full right (or left) stick, still holding the stick back (at the 4:30 position). Hold this position (1).</p>
<p class="ul">&bull; The plane should climb and roll inverted (2).</p>
<p class="ul">&bull; You'll dive and roll out level (3).</p>
<p class="ul">&bull; Stop the roll with your nose and wings level. Some forward stick toward the end of the roll may be necessary to bring the nose down (4).</p>
