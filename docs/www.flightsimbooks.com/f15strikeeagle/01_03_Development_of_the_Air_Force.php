<p class="p">The F-15's safety record is also extraordinary: It's the only fighter to complete its first 5000 hours of flight time without an accident.</p>
<h4 class="h4"><b>The F-15 in Active Service</b></h4>
<p class="p1">The first probation F-15 was delivered for active service to Luke Air Force Base on 14 November 1974. Since then, the F-15 has been deployed at United States Air Force (USAF) bases around the world. It also has been sold to Israel, Japan, and Saudi Arabia. Currently, the F-15 is being deployed to select units of the Air National Guard.</p>
<h4 class="h4"><b>F-15 Streak Eagle</b></h4>
<p class="p1">Computer projections at McDonnell Douglas predicted that the F-15 would easily beat many of the current time-to-altitude records. In early 1975, the Streak Eagle program went into operation, and at Grand Forks Air Force Base in North Dakota, a modified F-15 broke all existing time-to-altitude records. This one had been stripped of its gun, its radar, some avionics, the tail hook, one generator, some of the hydraulic system, and <a id="page_11"></a>the flap and speedbrake actuators. Even 40 pounds of external paint were removed.</p>
<p class="p">After achieving the 30,000 meter record (98,425 feet), the F-15 continued up to over 102,000 feet before falling over and starting its descent. This made it the obvious choice for delivering the ASAT antisatellite weapon.</p>
