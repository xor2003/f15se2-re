<h3 class="h3"><b>Development of the Air Force Type F-15 Aircraft</b></h3>
<p class="p1">The process of developing a military aircraft is a long and complicated affair. Often, the final product bears little resemblance to the original concept. Such was the case with the F-15.</p>
<p class="p">The F-15 program came out of the Air Force's experience during the early 1960s when it was felt that a new fighter plane was needed to replace the aging F-4 Phantom. While the F-4 was one of the most versatile aircraft ever developed, the Air Force believed there was a need for a pure air-combat fighter in the tradition of the P-51 Mustang and the F-86 Sabre.</p>
<h4 class="h4"><b>Project FX (Top Secret)</b></h4>
<p class="p1">The FX program (Fighter eXperimental) was the result. The first FX proposals were very heavy (60,000 pounds) and employed the then-fashionable <i>swing-wing</i> design. This being too much like the ill-fated F-111 design, the momentum swung in the other direction until 1967 when the conceived aircraft was down to 30,000 pounds.</p>
<p class="p">This 30,000-pound aircraft might have been developed had it not been for the Soviet Domodedovo Air Show in July 1967. It was there that the Soviets unveiled their new MiG-25, later designated <i>Foxbat</i> by NATO. The MiG-25 was capable of speeds up to Mach 2.8 and had an operational ceiling of 80,000 feet. It was immediately obvious that the current U.S. aircraft, the F-4 Phantom, was no match for the MiG-25.</p>
<p class="p">As a result, the FX project was expedited, and bids to develop and build the new jet fighter were received from many aircraft manufacturers. By December 1968, the field had been narrowed to three manufacturers: McDonnell Douglas Aircraft <a id="page_8"></a>Company, Fairchild Hiller, and North American. The project aircraft was then officially designated the <i>F-15</i>.</p>
<h4 class="h4"><b>Designation: <i>Eagle</i></b></h4>
<p class="p1">The name <i>Eagle</i>, however, wasn't immediately chosen. James McDonnell (or &ldquo;Mr. Mac,&rdquo; as he was known) preferred names derived from his interest in the occult&#8212;<i>Phantom, Voodoo, Banshee, Demon</i>, and the like. When he agreed to consider bird names, <i>Eagle</i> was proposed. Since the F-15 was designed to be an all-weather fighter, <i>Eagle</i> was chosen&#8212;someone had read in a wildlife book that eagles could hunt in bad weather.</p>
<p class="p">It was during this period that the possibility of using a modified version of the Navy's F-14 was first proposed. Congress wanted the Air Force and Navy to use the same aircraft; <i>commonality</i> was the latest buzzword. A number of studies, however, drew some conclusions: The F-14 wasn't maneuverable enough and required a two-man crew, something unacceptable to the Air Force. The idea of adapting the F-15 to function as a Navy carrier plane was also scrapped&#8212;costs would have increased while performance decreased with the addition of systems to use the Navy's <i>Phoenix</i> long-range missile.</p>
<h4 class="h4"><b>Vendor Selection</b></h4>
<p class="p1">In December 1969, McDonnell Douglas was named the winner of the F-15 contract, which called for 20 aircraft. Officially, the purpose of the program was &ldquo;to efficiently acquire a fighter capable of gaining and maintaining air superiority through air-to-air combat.&rdquo; The designers' philosophy became &ldquo;not a pound for air-to-ground.&rdquo; In other words, they were to build a pure dogfighter.</p>
<p class="p">The design-concept paper for the F-15 stated that the general mission of the aircraft was that of air superiority, broken down into subheadings of escorting strike forces over unfriendly airspace, fighter sweeps ahead of those strike forces, combat air patrol, and tactical intercept and defense of friendly territory.</p>
<p class="p">The most difficult of these missions, and the one most preferred by F-15 pilots, is the escorting or protecting of strike forces over enemy territory. Here exists the threat of antiaircraft artillery and surface-to-air missiles as well as enemy fighters directed by ground control.</p>
<a id="page_9"></a>
<h4 class="h4"><b>Delivery and ITP (Initial Test Program)</b></h4>
<p class="p1">On 27 July 1969, the F-15 made its first flight from Edwards Air Force Base in California. The initial test program went fairly smoothly, mainly due to the extensive wind-tunnel testing that was performed. The main changes made were increasing the size of the airbrake and changing the pressure required to operate the control stick.</p>
<em><b>Figure 1-1. The F-15 Eagle</b></em>
<p class="p1"><img src="009_1.jpg"/></p>
<em><b>Two F-15 Eagles flying in close formation. Note the AIM-9L Sidewinder missiles beneath each aircraft's wings.</b></em>
<p class="p">The initial weapons proposals also proved to be a problem. The F-15 was to have been fitted with the new GAU-7 25mm cannon, which used caseless ammunition&#8212;there were no metal shell casings that would have to be stored or ejected. Also, the new AIM-82 infrared missile was to be utilized. Both of these systems had numerous problems. The designers decided that since they were using a totally new airframe, new engines, and new avionics, they would do best to stick to tried-and-true weapons systems. The General Electric M61 20mm gun and the AIM-9L Sidewinder missile were selected because they had been used effectively for years.</p>
<p class="p">On 16 June 1972, the first manufactured F-15 rolled out of the McDonnell Douglas production plant in St. Louis.</p>
<a id="page_10"></a>
<h4 class="h4"><b>Replacement Comparison: F-15 Versus F-4</b></h4>
<p class="p1">The final design of the F-15 is a vast improvement over its predecessor, the F-4, and many of its systems are much easier to maintain and service. The F-15 has 67 quick-access doors, for instance&#8212;four times the number on the F-4. Also, the re-launch turnaround time is 12 minutes&#8212;45-percent faster than that of the F-4.</p>
