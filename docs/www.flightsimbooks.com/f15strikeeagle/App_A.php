<h1 class="h1">APPENDIX A<hr/><br/>Suggested Reading List</h1>
<p><hr/></p>
<p class="p1">If you want to learn more about air combat or the F-15, I suggest you take a look at the following books.</p>
<p class="p1"><b>Books on Air Combat</b></p>
<p class="bib">Franks, Norman. 1986. <i>Aircraft Versus Aircraft,</i> New York: Macmillan.</p>
<p class="bib">Gunston, Bill, and Lindsay Peacock. 1989. <i>Fighter Missions.</i> New York, New York: Orion Books.</p>
<p class="bib">Gunston, Bill, and Mike Spick. 1983. <i>Modern Air Combat.</i> New York: Crescent Books.</p>
<p class="bib">Nordeen, Lon O., Jr. 1985. <i>Air Warfare in the Missile Age.</i> Washington, D.C.: Smithsonian Institute Press.</p>
<p class="bib">Shaw, Robert L. 1985. <i>Fighter Combat: Tactics and Maneuvering.</i> Annapolis, MD: Naval Institute Press.</p>
<p class="bib">Sims, Edward H. 1972. <i>Fighter Tactics and Strategy 1914-1970.</i> Fallbrook, CA: Aero Publishers.</p>
<p class="bib">Spick, Mike. 1983. <i>Fighter Pilot Tactics: The Techniques of Daylight Air Combat.</i> New York: Stein and Day Publishers.</p>
<p class="bib">Spick, Mike. 1988. <i>The Ace Factor.</i> Annapolis, Maryland: Naval Institute Press.</p>
<p class="p1"><b>Books on the F-15</b></p>
<p class="bib">Ethel, Jeff. 1981. <i>F-15 Eagle.</i> London, England: Ian Allan Ltd. Gething, Michael. 1983. Vol. 1. <i>Modern Fighting Aircraft: F-15.</i> New York: Arco Publishing.</p>

