<h3 class="h3"><b><i>F-15 Strike Eagle I</i></b></h3>
<h4 class="h4"><b>Simulation Setup for Air Training and Practice</b></h4>
<p class="p1"><b>Skill level:</b> Rookie</p>
<p class="p1"><b>Mission:</b> &#35;1, Libya</p>
<p class="ul">&bull; Start the game and take off.</p>
<p class="ul">&bull; Arm and fire a short-range missile at the MiG that pops up in front of you. If this doesn't destroy it, follow and fire a second missile.</p>
<p class="ul">&bull; Fly the plane back out to sea, away from the land mass.</p>
<p class="ul">&bull; Either place the NAV cursor over the base or use the Horizontal Situation Display (map) to guide you.</p>
<p class="ul">&bull; On your way out to sea, drop all bombs. This will improve the performance and handling of the aircraft.</p>
<a id="page_58"></a>
<p class="p">Once you've completed this checklist, you're ready to practice maneuvers. Should an enemy aircraft appear on radar or should a target-designator box appear on the screen, fire a medium-range missile immediately. If this missile misses, grit your teeth and go after the target. Keep in mind, the best way to learn to fight is by fighting.</p>

