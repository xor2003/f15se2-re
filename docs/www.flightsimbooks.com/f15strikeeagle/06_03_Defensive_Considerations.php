<h3 class="h3">Defensive Considerations</h3>
<h4 class="h4">Jinking a Missile Coming Head-On</h4>
<p class="p1"><b>Situation:</b> Radar shows a missile heading toward you from in front.</p>
<p class="p1"><b>Maneuver:</b></p>
<ul type="bull">
<li><p class="p1">Determine if the missile is heat-seeking or radar-homing. Head straight toward it (1).</p></li>
<li><p class="p1">When the missile is 1 to 1&frac12; radar grid divisions away (approximately 10-15 miles), release the proper countermeasure (flare for heat-seeker, ECM for radar-homing). Pull up hard(2).</p></li>
<li><p class="p1">Watch the missile pass beneath you (3).</p></li>
</ul>
