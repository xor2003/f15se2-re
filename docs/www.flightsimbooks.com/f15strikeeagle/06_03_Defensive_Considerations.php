<h4 class="h4">Avoid Enemy Overflight</h4>
<p class="p1">Radar isn't the only way the enemy can locate you. It can also &#34;see&#34; and &#34;hear&#34; you if you fly too close to an enemy location. Therefore, if you don't intend to bomb a target, it's to your advantage to fly around it rather than directly over it. Otherwise, you may have a lot of company.</p>
<p class="p">On the other hand, if you're looking for trouble, flying over an airfield should generate a lot of action.</p>

