<h1 class="h1">Foreword</h1>
<h4 class="h4"><center>by Sid Meier</center></h4>
<p><hr/></p>
<p class="p1">Way, way back in computer history&#8212;when Donkey Kongs and Froggers roamed the earth&#8212;MicroProse decided to see if humans were ready for a higher game form. It was 1985 when we put the original <i>F-15 Strike Eagle</i> &ldquo;out there.&rdquo;</p>
<p class="p">It was the first game to require fast and frequent decision making, based on highly-complex, high-intensity situations. While other games required immediate and reflexive reaction on the joystick, <i>F-15 Strike Eagle</i> introduced a new&#8212;and definitely more exciting&#8212;requirement: immediate and <i>reflective</i> action. You had to <i>think</i> about what you were doing, and think fast. You couldn't just blast anything in sight.</p>
<p class="p">First of all, there might be too many things to shoot. Also, the current weapons might not have sufficient range or might not be the right weapon for a particular target. Maybe you didn't have time to switch to the right weapon. And maybe instead of shooting somebody, your primary concern right then was to keep from being shot. And maybe if you did a scissors turn, that would shake the guy off your tail, knock out the other guy, and get you lined up for a bombing run all at once. Decisions. Decisions. Decisions. Just like in real life.</p>
<p class="p">Since that early dawn of sophisticated computer gaming, MicroProse has learned a lot about computer combat simulation. If you have <i>F-19 Stealth Fighter,</i> our winner of the Simulation of the Year Award for 1988, you've seen much of it: spectacular polygon-based 3-D graphics; thousands of landmarks and terrain features based on the actual geography of the region being simulated; an almost infinite array of missions, each with a primary and secondary objective; and more sophisticated artificial intelligence to make enemies respond authentically in any situation arising on any of the missions.</p>
<p class="p">But, <i>F-19</i> and <i>F-15</i> are two entirely different games. <i>F-19</i> requires you to move toward your target quietly, carefully avoiding enemy confrontation for as long as possible. In <i>F-15,</i> on the other hand, direct enemy contact is unavoidable. The accent, therefore, is on fast action and plenty of dogfights.</p>
<a id="page_vi"></a>
<p class="p">We knew that incorporating the technological enhancements of <i>F-19</i> into <i>F-15</i> would result in two distinct games, each with its own powerful appeal. There is probably no greater testament to the strength of <i>F-15 Strike Eagle II</i> than that a computer entertainment writer of Rich Sheffield's caliber would devote the time and energy to write a full length book about it.</p>
<p class="p">Thanks for your interest in MicroProse. Keep your eye on us; we have a host of terrific new games heading your way.</p>
<p class="p">Have a great time with <i>F-15 Strike Eagle II.</i></p>

