<h4 class="h4">Skill Levels</h4>
<p class="p"><b>Arcade.</b> This level should only be used to familiarize yourself or a new user with the plane's basic operation. The aircraft won't bank in a turn in this mode. One gun hit destroys the opponent.</p>
<p class="p"><b>Rookie.</b> (Easy) Beginners should start here. The plane operates correctly and two gun hits are required to down an enemy aircraft.</p>
<p class="p"><b>Pilot.</b> (Moderate) Things begin to get more difficult here. The opposing pilots are much better, but will occasionally attempt to run after a long, turning fight, giving you a good missile shot or guns opportunity. At least three gun hits are required to score a kill. Bombing and returning to base must be more precise. SAMs are more accurate.</p>
<p class="p"><b>Ace.</b> (Tough) SAMs are very accurate. Enemy pilots are very good and won't disengage. Keep an eye on your energy level and altitude as enemy pilots will turn you right into the ground if you're not careful. At least four gun hits are required<a id="page_137"></a> to down an enemy, and often more. Bombing must be precise, and returning to base, you must be well under 3000 feet. Unless your dogfighting skills are very good, stay away from turning fights, and use missile attacks only.</p>

