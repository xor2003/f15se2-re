<h4 class="h4">Accumulating Points</h4>
<p class="p1">If you're competing against another pilot, you'll want to accumulate as many points as possible. Points awarded for enemy kills increase with the difficulty level. Destroying a primary ground target results in the most points. Although primary targets can be destroyed with one bomb, it's possible to drop more than one on a target in a single bombing run and get point credit for all hits.</p>
<p class="p">If you have a primary target lined up properly, and you have the time (uninterrupted by SAMs or enemy aircraft), you may be able to drop as many as four bomb loads before finishing your pass. If all four hit, you'll quadruple your point total.</p>

