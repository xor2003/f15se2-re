<h3 class="h3">OPFOR</h3>
<p class="p1">It's very likely that you'll face a wide variety of Opposing Forces (OPFOR) weapons that range from the latest Soviet developments, to modern NATO equipment sold earlier to Iran, to Korean War-era MiG-17s. Some of this equipment is certainly better than others, but all of it can&#8212;and will&#8212;kill you if you're not prepared and knowledgeable about its capabilities.</p>
<p class="p">This section will give you a quick rundown of the enemy equipment you'll face and some basic information on each system.</p>
