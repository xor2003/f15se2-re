<h3 class="h3"><b>ACM Checkride &#35;1</b></h3>
<p class="p1">This first checkride is just to get you comfortable following an enemy aircraft and keeping him in your sights. Doing this will require using many of the basic maneuvers you've learned&#8212;rolls and turns as well as wise use of the throttle and speedbrake.</p>
<h4 class="h4"><b>Simulation Setup for Checkride &#35; 1</b></h4>
<p class="p1">(<i>Setup and objectives are the same for both versions of</i> F-15 Strike Eagle. <i>You may choose to use the Alt-T function in</i> F-15 II <i>to set the game to Training mode</i>.)</p>
<p class="p1"><b>Level:</b> Rookie</p>
<p class="p1"><b>Mission:</b> Libya</p>
<p class="p1"><b>Objective:</b> To keep the enemy in your sights for at least 30 seconds.</p>
<p class="p1"><b>Procedure:</b> When you start the simulation, a MiG should appear in close proximity to your position. Pull behind the bogey and try to stay on his tail and keep him in the circle on your HUD. When you've been on his tail for 30 seconds, feel free to down the bogey with a missile shot or guns, and then return to the base.</p>
<p class="p">When you can successfully complete this ride on the rookie level, move up to the next level and try it again. It will be harder on the next level, as the enemy pilot will use vertical climbing and diving maneuvers to try and shake you. Be prepared to use the afterburners to keep from stalling in a climb and the speedbrake to keep from overshooting in tight turns. Keep the bad guy in front of you at all times.</p>

