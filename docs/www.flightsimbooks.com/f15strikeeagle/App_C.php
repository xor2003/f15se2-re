<h1 class="h1">APPENDIX C<hr/><br/>Definition of Terms</h1>
<p><hr/></p>
<p class="p1"><b>ballistic</b></p>
<p class="p1">Usually refers to climbing straight up so that altitude is gained due to engine power, not wing lift.</p>
<p class="p1"><b>bandit</b></p>
<p class="p1">An enemy aircraft.</p>
<p class="p1"><b>belly-check</b></p>
<p class="p1">Usually a 360-degree roll to check for enemy aircraft while in the blind spot beneath the aircraft.</p>
<p class="p1"><b>black boxes</b></p>
<p class="p1">Electronic components for various aircraft systems mounted in removable boxes to facilitate rapid change and maintenance.</p>
<p class="p1"><b>bogey</b></p>
<p class="p1">An enemy aircraft.</p>
<p class="p1"><b>bounce</b></p>
<p class="p1">To attack an enemy aircraft, usually from above.</p>
<p class="p1"><b>canard</b></p>
<p class="p1">Small wings, usually mounted toward the nose of an aircraft to improve maneuverability.</p>
<p class="p1"><b>chaff</b></p>
<p class="p1">Thin, light strips of foil used to create a large return on an enemy radar. Used to decoy radar-guided missiles.</p>
<p class="p1"><b>closing</b></p>
<p class="p1">Reducing the distance between your aircraft and another.</p>
<p class="p1"><b>cold-side</b></p>
<p class="p1">The underside or belly of an aircraft.</p>
<a id="page_206"></a>
<p class="p1"><b>dogfighter</b></p>
<p class="p1">Aircraft designed primarily for air-to-air combat.</p>
<p class="p1"><b>Doppler</b></p>
<p class="p1">A radar system that makes use of the shift in frequency of signals reflected from earth, ahead or behind an aircraft, to obtain its true airspeed and location.</p>
<p class="p1"><b>envelope</b></p>
<p class="p1">The performance limits of an aircraft.</p>
<p class="p1"><b>flare</b></p>
<p class="p1">Very hot decoys used to fool heat-seeking missiles.</p>
<p class="p1"><b>gomer</b></p>
<p class="p1">An enemy.</p>
<p class="p1"><b>guns kill</b></p>
<p class="p1">To destroy an enemy aircraft at close range with an onboard cannon&#8212;the most difficult type of kill.</p>
<p class="p1"><b>hot-side</b></p>
<p class="p1">The top of an aircraft.</p>
<p class="p1"><b>HOTAS</b></p>
<p class="p1">Hands on throttle and stick.</p>
<p class="p1"><b>infrared</b></p>
<p class="p1">The portion of the energy spectrum felt as heat but not seen.</p>
<p class="p1"><b>inverted</b></p>
<p class="p1">Flying an aircraft upside-down, or with the canopy toward the ground.</p>
<p class="p1"><b>jamming</b></p>
<p class="p1">Emitting signals so strong that they overwhelm an enemy's detection system and render it useless.</p>
<p class="p1"><b>jink</b></p>
<p class="p1">To make wild and rapid movements with the aircraft, both up and down and side to side, usually in an attempt to avoid an incoming missile or to evade an enemy aircraft.</p>
<p class="p1"><b>klick</b></p>
<p class="p1">One kilometer or 1000 meters.</p>
<a id="page_207"></a>
<p class="p1"><b>multibogey</b></p>
<p class="p1">More than one enemy aircraft in the combat area.</p>
<p class="p1"><b>pipper</b></p>
<p class="p1">The circle or dot used for aiming the cannon or for lining up a bomb drop.</p>
<p class="p1"><b>pod</b></p>
<p class="p1">Streamlined containers used to carry equipment under an aircraft's wing or belly.</p>
<p class="p1"><b>punch out</b></p>
<p class="p1">To activate the ejection seat during flight.</p>
<p class="p1"><b>retarded</b></p>
<p class="p1">A bomb that employs fins to increase its drag so it can be dropped from low altitude without catching the aircraft in the bomb blast.</p>
<p class="p1"><b>signature</b></p>
<p class="p1">Characteristic &#34;fingerprint&#34; every type of radar equipment emits.</p>
<p class="p1"><b>six</b></p>
<p class="p1">The 6 o'clock position behind an aircraft. Directly in front is 12 o'clock; directly behind is 6 o'clock.</p>
<p class="p1"><b>slick</b></p>
<p class="p1">Bombs without retarding fins.</p>
<p class="p1"><b>smart</b></p>
<p class="p1">A bomb with an onboard system that can identify a target and adjust its flight path to ensure a hit.</p>
<p class="p1"><b>stall</b></p>
<p class="p1">A condition in which there isn't enough air moving over an aircraft's wing to produce adequate lift to keep the aircraft under control.</p>
<p class="p1"><b>wild weasel</b></p>
<p class="p1">An aircraft assigned the mission of leading an attack and identifying and destroying SAM sites.</p>

