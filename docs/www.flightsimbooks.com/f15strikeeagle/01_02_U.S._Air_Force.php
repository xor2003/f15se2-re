<h3 class="h3"><b>U.S. Air Force Historical Background</b></h3>
<h4 class="h4"><b>The Air Force Story</b></h4>
<p class="p1">Although the Air Force is the newest military service of the U.S., it has packed a tremendous amount of growth into its relatively short life. Straight thinking, hard work, and courageous men have helped the Air Force become today's most potent aerospace force. Reaching this status is a remarkable achievement&#8212;the U.S. Air Force had very meager beginnings indeed.</p>
<p class="p">On 1 August 1907, the Signal Corps of the U.S. Army established an Aeronautical Division to &ldquo;Take charge of all matters pertaining to military ballooning, air machines, and all kindred subjects.&rdquo; The new division was assigned a total of two members, and one of those went &ldquo;over the hill&rdquo; shortly thereafter, leaving the Army with a one-man division.</p>
<p class="p">Shortly, a contract was issued to the Wright Brothers to build a plane and to train several pilots. Three pilots eventually soloed, but two were transferred back to their original duties after the only plane was damaged in a crash. When the training facility was moved from Maryland to Texas, the lone pilot had to continue his Wright Brothers training by mail. Until 1911, the Army had only one pilot and only one plane.</p>
<p class="p">The Army flying program received little backing and support. By 1914 the U.S. had lost the lead in aeronautics and had fallen very far behind in military aviation. Safely surrounded <a id="page_4"></a>by oceans, the U.S. didn't feel called upon, before World War I, to go with the Europeans in the air.</p>
<p class="p">On 18 July 1914, Congress finally enacted legislation to give Army aviation recognition and put it on a firm and permanent basis. The act created the Aviation Section of the Signal Corps and authorized 60 officers and 260 enlisted men. However, a real air section had yet to be created, as was proven when the 1st Aero Squadron accompanied General Pershing across the Mexican border in pursuit of Pancho Villa in 1916. The eight Curtis aircraft they had lasted only six weeks. Six were crashed beyond repair and the other two were condemned and destroyed. It appeared that the 1st Aero Squadron's only value was for short mail flights&#8212;if the weather was good.</p>
<p class="p">When World War I broke out, the Europeans had a healthy lead in aviation. Aircraft intended for reconnaissance only quickly began shooting at each other and air warfare was born. When the Germans developed the synchronizing gear to let machine gun bullets pass between the spinning propeller blades, dogfighting really took off.</p>
<p class="p">When the U.S. finally declared war on Germany and entered World War I, we had only 35 pilots and no aircraft suitable for war. Finally realizing the need for air power, the Congress appropriated $640 million for America's air arm.</p>
<p class="p">The first American flyers to face the enemy were volunteers in the French unit of the Lafayette Escadrille, but by March 1917, the 94th and 95th Pursuit Squadrons were patrolling the front lines.</p>
<p class="p">On 20 May 1918, Army Aeronautics was removed from the Signal Corps and established as a separate bureau, and by that September, 1500 planes were ready for a major assault on Germany. Although we had entered the war woefully unprepared, the U.S. pilots quickly showed how capable they were of fighting with the enemies. When the war ended, we had 45 squadrons in service with 767 pilots. These men downed 781 enemy planes and 75 balloons while losing only 289 planes and 48 balloons.</p>
<p class="p">Between the wars, the Air Service fought mostly with the Navy, but it did take a step toward becoming a separate arm of the military in 1926 when it became the Air Corps, still under the Army.</p>
<p class="p">When the Japanese attacked Pearl Harbor in 1941, the Air Corps was put on the defensive again, but it quickly mobilized <a id="page_5"></a>and went on the offensive with Jimmy Dolittle's famous Tokyo raid off the USS <i>Hornet</i>. Once the U.S. Air Services started running, they never looked back. They bombed Germany into submission and, of course, delivered the nuclear knockout punch to Japan in a B-29 named the <i>Enola Gay</i>.</p>
<p class="p">Finally, on 18 September 1947, the Army Air Force became the United States Air Force&#8212;on equal footing with the U.S. Navy and Army. With the war over, the U.S. began building an Air Force for peacetime in the form of the Strategic Air Command. The Tactical forces, however, were still around and performed bravely in Korea and Vietnam, and the Air Force's airlift capability was displayed before the world during the Berlin Airlift.</p>
<h4 class="h4"><b>Functions of the Department of the Air Force</b></h4>
<p class="p1">The Department of the Air Force is responsible for the preparation of the air forces necessary for the effective prosecution of war and military operations short of war, and under integrated joint mobilization plans for the expansion of the peace-time component of the Air Force to meet the needs of war. Within the Department of the Air Force, the Air Force includes combat and service aviation forces.</p>
<p class="p">Some major functions of the Air Force are to:</p>
<p class="ul">&bull; Organize, train, equip, and provide forces for the conduct of prompt and sustained combat operations in the air&#8212;specifically, forces to defend the United States against air attack, gain and maintain air superiority, defeat enemy air forces, conduct space operations, and control vital air areas.</p>
<p class="ul">&bull; Organize, train, equip, and provide forces for appropriate air and missile defense and space operations, including forces for strategic defense of the United States in accordance with joint doctrines.</p>
<p class="ul">&bull; Organize, train, equip, and provide forces for strategic air and missile warfare.</p>
<p class="ul">&bull; Organize, equip, and provide forces for joint amphibious, space, and airborne operations.</p>
<p class="ul">&bull; Organize, train, equip, and provide forces for close air support and air logistic support to the Army and other forces, including airlift, air support, reshuffle of airborne operations, aerial photography, tactical air reconnaissance, and air interdiction of enemy land forces and communications.</p>
<a id="page_6"></a><p class="ul">&bull; Organize, equip, train, and provide forces for air transport of the Armed Forces.</p>
<p class="ul">&bull; Develop doctrine, procedures, and equipment for air defense from land areas.</p>
<p class="ul">&bull; Furnish launch and space support for the Department of Defense.</p>
<p class="ul">&bull; Organize, train, equip, and furnish land-based tanker forces for in-flight refueling support of strategic operations and deployments of aircraft of the armed forces and Air Force tactical operations.</p>
<p class="ul">&bull; Organize, train, equip, and furnish forces to operate air lines of communication.</p>
<p class="ul">&bull; Organize, train, equip, and furnish forces for the support of special operations.</p>
<p class="p">Collateral functions include:</p>
<p class="ul">&bull; Surface sea surveillance and antisurface ship warfare through air operations.</p>
<p class="ul">&bull; Antisubmarine warfare and anti-air warfare operations to protect sea lines of communications.</p>
<p class="ul">&bull; Aerial minelaying operations.</p>
<p class="ul">&bull; Air-to-air refueling in support of naval campaigns.</p>
<h4 class="h4"><b>Formation of the Eagle Rapid Deployment Team</b></h4>
<p class="p1">As a result of the complexity and ineffectiveness of the 1986 Air Force and Navy strike against Libya, it was determined that a rapidly deployable air strike unit was needed. The Eagle Rapid Deployment Team was established in 1990 to meet this need. This team is capable of deploying to anywhere in the world and carrying out an air strike in a matter of hours, not days.</p>
<p class="p">A major objective in the unit's formation was to keep the equipment and personnel needed to an absolute minimum. The F-15E Strike Eagle was, then, a perfect match for this mission&#8212;able to deliver a substantial weapons load deep in enemy territory, and able to defend itself from hostile air attack, eliminating the need for additional fighter air cover.</p>
<p class="p">Team personnel are kept on standby 24 hours a day and can be airborne en route to a threat area in a matter of minutes. With its organic air refueling and logistics component, the fighters can be flown directly to the nearest airfield and <a id="page_7"></a>loaded with the ordinance needed to carry out the mission, and then a second fighter crew can take off immediately for the target area. This team is trained to deal with the various contingencies and problems caused by rapidly developing situations and hastily drawn up plans.</p>
<p class="p">Transfer to the team is by invitation only&#8212;no volunteers. It's the tip of the Air Force's tactical spear and certainly the most likely to see combat action and to face hostile fire. Keep this in mind as you study and train.</p>

