<h4 class="h4">Totally Aggressive</h4>
<p class="p1">To carry out a totally aggressive attack on two opponents may be exciting, but it's also very dangerous (or as dangerous as a simulation can get). History, however, is full of successful one-on-two accounts, so if the right opportunity presents itself, don't hesitate.</p>
<p class="p">To aggressively attack two opponents, your bombs should be gone, and you should have plenty of fuel and at least two Sidewinders and two Sparrows.</p>
<ul type="bull">
<li><p class="p1">Fire off a Sparrow at one opponent to keep him busy for a minute or so (1).</p></li>
<li><p class="p1">Head straight for enemy aircraft &#35;2. You'll certainly have to dodge missiles here, so keep an eye out. An extra pair of hands and eyes here is very helpful (2).</p></li>
<li><p class="p1">As you approach &#35;2, cut power and try to out-turn your opponent for a gun shot (3).</p></li>
<li><p class="p1">Keep an eye on the Sparrow that's chasing target &#35;1. If the missile hits or runs out of fuel, fire a Sidewinder at &#35;2.</p></li>
<li><p class="p1">If the Sparrow you first shot missed and a Sidewinder shot at &#35;2 also misses, break off the attack. Disengage and build up some airspeed; then repeat this procedure until one of the targets goes down.</p></li>
<li><p class="p1">Move in for a one-on-one attack.</p></li>
</ul>
