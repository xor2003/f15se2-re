<h3 class="h3">Multiple Bogey Situations</h3>
<p class="p1"><i>Sometimes you'll face two opponents, not just one. Learn how to handle them both and survive.</i></p>
<p class="p1">Occasionally, two enemy aircraft will appear on your radar. Both may even fire missiles at you. What you do at this point depends on a number of factors:</p>
<ul type="bull">
<li><p class="p1">Your altitude.</p></li>
<li><p class="p1">Your energy level.</p></li>
<li><p class="p1">Your mission: Are you closing on a bombing target?</p></li>
<li><p class="p1">Weapons available: Don't attack two planes when you have only one missile left.</p></li>
<li><p class="p1">Number of bombs carried: Speed and maneuverability both suffer with heavy bomb loads.</p></li>
<li><p class="p1">Fuel left and distance to base.</p></li>
</ul>
<p class="p">Two against one is a serious situation, and you must weigh all these factors before choosing your course of action. Depending upon your situation, you'll take a <i>totally defensive, cautiously aggressive,</i> or <i>totally aggressive stance.</i></p>
<a id="page_125"></a>
<h4 class="h4">Totally Defensive</h4>
<p class="p1">When entering into an attack on two opponents, you're violating Oswald Boelcke's first rule of air combat: <i>Always attack with an advantage.</i> Reconsider if you have several other factors against you, such as low fuel or control problems due to previous missile hits. If you decide to make a run for it, here's what you should do:</p>
<ul type="bull">
<li><p class="p1">Drop all bombs.</p></li>
<li><p class="p1">Fire a missile at one of the targets, even if you're out of range&#8212;you may get lucky.</p></li>
<li><p class="p1">If you have the necessary altitude, perform a Split-S and begin an unloaded acceleration toward base (use the afterburners if you have enough fuel).</p></li>
<li><p class="p1">If you're at low altitude and have plenty of fuel, use your afterburners either to escape at low altitude or go straight up and hope they can't follow you.</p></li>
</ul>
<a id="page_126"></a>
<h4 class="h4">Cautiously Aggressive</h4>
<p class="p1">To attack two opponents, your aircraft must be undamaged and you should have adequate fuel and weapons. But maybe you're still carrying bombs you'll need for a ground attack later; now is a good time to consider a cautious attack.</p>
<ul type="bull">
<li><p class="p1">Arm and fire a medium-range missile at one opponent (1).</p></li>
<li><p class="p1">Light the afterburners and fly head-on toward the second aircraft, offering the worst possible missile target (2).</p></li>
<li><p class="p1">Pass beneath the enemy and try to obtain 10-20 miles of separation.</p></li>
<li><p class="p1">Shut down the burners, but stay at 100-percent power.</p></li>
<li><p class="p1">Keeping up your speed, make a broad sweeping turn, staying out of short-missile range but within range for your medium-range missiles (3).</p></li>
<li><p class="p1">Keep firing Sparrows at your opponent until one strikes home.</p></li>
<li><p class="p1">Close in for a one-on-one with the other target.</p></li>
</ul>
