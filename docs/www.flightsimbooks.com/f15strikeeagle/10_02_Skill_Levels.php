<h3 class="h3"><b>Skill Levels</b></h3>
<p class="p1">One of the first things you must choose before a mission is the <i>skill level</i> at which you want to play: Rookie, Pilot, Veteran, or Ace. This is a very important decision that will affect a number of game parameters. So that you'll have some idea of what you're getting into, the following are some of the major game areas affected by the skill level selection.</p>
<p class="p"><b>Enemy aircraft tactics.</b> As the skill level goes up, the number of evasive maneuvers available to enemy pilots increases. At the easiest levels, enemy pilots will do little more than turn to try and get away from you, and they may turn and head for home. Once you reach the higher levels, the bad guys have a much larger bag of tricks. Here, they're much more likely to make vertical turns and quick dives as they try to shake you. Once engaged, it's either you or them&#8212;they won't turn and run.</p>
<a id="page_170"></a>
<p class="p"><b>Damage levels.</b> The higher up you go, the easier it is to get shot down. At Rookie level it seems you can take an unlimited number of hits, but at Ace level you'll notice a sharp drop in engine power with each hit.</p>
<p class="p"><b>SAM locations.</b> As you move up in skill level, the number of SAM locations increases. Not only will there be more of them, there will be more of the deadly Doppler-guided SA-10 and SA-12 sites as well. And if that's not enough, these sites will also have faster reaction times and faster reload times at higher levels. This means they'll react to your presence and shoot at you sooner, and they'll be able to fire more missiles in a given period of time. At lower levels, you may notice that if you can avoid the first SAM fired from a site, you may be able to get close enough to destroy it before it can shoot again. At high levels, you won't have that luxury; the enemy may be able to get off three, four, or even five missiles as you approach.</p>
<p class="p"><b>Gunsight kill area.</b> At rookie level it seems that as long as an enemy plane is somewhere in front of you, a quick burst from the gun will kill it. As you progress, this &ldquo;kill area&rdquo; becomes much smaller, requiring you to shoot much better and to time your shots just right.</p>
<p class="p"><b>Number of enemy aircraft.</b> Not only are SAM sites more numerous at high skill levels, bad guys in the air are also more plentiful. That, however, can be a blessing in disguise&#8212;more planes means more targets, and that means a higher score!</p>

